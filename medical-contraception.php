                <h2 id="medical-04">信じてはいけない誤った避妊法</h2>
                <figure class="section_eyecatch float-right ml-2 mb-2">
                  <img src="images/medical-guide/single/medical-contraception03.jpg" alt="" class="img-fluid img-thumbnail">
                </figure>
                <p>自分がEDかどうか知るために、勃起不全がどのような障害かをわかりやすく解説していきます。</p>
                <p>誤った避妊方法は、膣外射精・膣内洗浄や、安全日の捉え方の誤りがあげられます。</p>
                <p>男性が膣外で射精しても、射精の前後で精子が女性の体内に入る可能性は大いにあります。<br>
                また、性行為後にビデや炭酸水などで膣内を洗っても、卵子に到達しようとしている精子を洗い流せるわけがありません。</p>
                <p>パソコンやスマホで閲覧できるインターネット情報には、誤った情報もたくさん流れています。情報を選別して正しい避妊方法を学び、男女共に協力して実践することが大切です。</p>
                <p>望まない妊娠を避け、堕胎の悲しみや痛みを回避するために、そしてすべての子供の誕生が喜ばしいことであるために、<span>誤った情報に惑わされず正しい避妊方法を身につけましょう。</span></p>
                <h3>膣外射精（外出し）</h3>
                <p>膣外射精とは、性交中に男性が女性の膣内で射精しないようにするため、射精の直前に膣から陰茎を引き抜き、膣以外の場所で射精することをいいます。<br>
                俗に「外出し」と呼ばれる方法です。</p>
                <p>実は多くの方が、膣外射精を避妊方法として捉えています。</p>
                <p>女性の口腔内や腹部、胸部、臀部などに射精する描写が多いアダルトビデオの影響や、誤ったインターネット情報に惑わされ、<span>「外出しすれば妊娠しない」と勘違いしている男女が非常に多い</span>のです。</p>
                <p>その結果、望まない妊娠・出産という悲しい結末を迎えるケースも多々散見されます。</p>
                <p>男性器からは、射精前にもカウパー腺液という分泌液がでています。<br>
                この分泌液の中には精子が含まれているので、射精時だけ膣から男性器を抜いたとしても、全く避妊できていないのです。</p>
                <p><span>射精のタイミングがズレる場合もありますので、避妊方法としてはおすすめできません。</span></p>
                <p>性感染症に関しても、コンドームと比べると感染リスクが高いことはいうまでもありません。</p>
                <p>性感染症の心配の少ない夫婦関係である場合は、コンドームよりも簡便な避妊方法として、他の避妊方法と併用する形で実践していることもあるようですが、確実性の低い方法に過ぎないと捉えておくべきです。</p>
                <h3>膣内洗浄</h3>
                <p>ムードに任せて膣内射精をしてしまったけれども、あとで洗い流せば避妊できると考えている方も、たくさんおられるようです。</p>
                <p>あるアンケート調査によると、実践している避妊方法を尋ねると、回答が最も多かったのがコンドームの着用、次いで多かったのが膣外射精でした。<br>
                性交中絶法と呼ばれていたこともありますが、現在では避妊方法としては間違っているとされています。</p>
                <p>温水洗浄機能付きのトイレにあるビデ機能で膣を洗い流す方法は、避妊とは程遠いものです。</p>
                <p>膣内で射精されたあと、数億個の精子は分速2~5mmというスピードで一斉に子宮を泳ぎ、卵子を目指します。<br>
                卵子にたどり着き、受精できるのは膨大な数の精子のなかのたった1つですから、競争のごとく速く女性の体内に入っていくのです。</p>
                <p>このスピードでは、ビデ機能を使っても到底追いつくことはできません。よって<span>ビデ機能による膣内洗浄で避妊することは不可能です。</span></p>
                <p>同じ理論で、炭酸水や他の液体で膣を洗浄しても、避妊にはなりません。</p>
                <p>また、精子は卵子と違い、最長で1週間ほど生き延びます。性交後に排卵が起こった場合にも受精の可能性はおおいにありますので、知識として知っておきましょう。</p>
                <h3>安全日・生理日の認識</h3>
                <p>避妊方法のひとつとして誤って捉えられている情報の一つに、「安全日」という言葉があります。</p>
                <p>世間では、「安全日とは妊娠しない日のことである」と認識されているかもしれませんが、これは間違いです。</p>
                <p>生理周期や基礎体温の上下により排卵日を特定し、妊娠しにくい日を推測することは可能です。しかし残念ながら、<span>女性には「絶対に妊娠しない日」は存在しません。</span></p>
                <p>排卵を過ぎて卵子の寿命が尽きた日以降、生理が終わるころまでは妊娠しにくいとおこなわれています。卵子の寿命は約1日ですので、排卵から2～3日たてば妊娠しにくい時期に入っていると考えられます。</p>
                <p>とはいえ、排卵のタイミングや卵子の寿命、パートナーの精子の寿命も人それぞれ異なりますので、絶対妊娠しないとはおこない切れません。</p>
                <p><span>生理中に性交しても妊娠しない、という説も間違いです。</span></p>
                <p>精子の寿命は長ければ1週間と考えられているので、女性の体内に残っていた精子が排卵時に卵子と一緒になり受精する可能性もあります。</p>
                <p>生理日や安全日はあくまでも参考として考え、パートナーにも協力をあおぎ、他の避妊方法を合わせて実践するようにしましょう。</p>
              </section>

              <section>
                <h2 id="medical-05">避妊の必要性</h2>
                <figure class="section_eyecatch float-right ml-2 mb-2">
                  <img src="images/medical-guide/single/medical-contraception04.jpg" alt="" class="img-fluid img-thumbnail">
                </figure>
                <p>望まない妊娠による人工妊娠中絶は、20歳未満に限定すると、妊娠してから満8週以上経過している割合が半数以上を占めます。</p>
                <p>妊娠を望む夫婦であれば妊娠検査薬の陽性反応を認識したのち産婦人科を受診し、胎嚢（たいのう）や心拍を測定から、妊娠の可否を診断されます。</p>
                <p>しかし望まずに妊娠した場合、<span>身体の異変を感じて受診したときには時すでに遅しというケースも多く、周りに相談もできずに悩みを抱える女性もたくさんいます。</span></p>
                <p>中絶できる期間を過ぎ、人工的な死産というかたちをとる場合はまだ良く、最悪の場合誰にも知られずに自宅で出産し赤ちゃんを遺棄する事件に発展することもあります。</p>
                <p>出産という決断をしたとしても、幼児虐待に発展したり、育児ノイローゼになったりするリスクも高まってしまいます。</p>
                <p><span>悲しい事態を避け、女性の心と身体にも負担をかけないために正しい避妊方法を勉強し実践することが大切です。</span></p>
                <p>避妊は、パートナーとの協力と相互理解が不可欠です。<br>
                誤った方法での避妊は、性暴力ととらえられかねません。一時の快楽に流されず、お互いをいたわり楽しい性生活を実現しましょう。</p>
