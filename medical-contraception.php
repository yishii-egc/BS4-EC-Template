                <h2 id="medical-02">妊娠とは</h2>
                <p>妊娠とは、<span>子供を望む男性と女性が避妊をせずに性交をし、やがて出産に至るまでの生理的経過</span>を指します。</p>
                <p>医学的には、哺乳類の胚が胎盤を形成し、母体から栄養を貰いながら発生を進める現象と説明されます。</p>
                <p>赤ちゃんの誕生につながる妊娠はすべてが喜ばしいことであるべきですが、現実には、望まれない妊娠も後を絶ちません。</p>
                <p>堕胎を選択され世に生まれることのできない悲しい赤ちゃんを一人でも減らすために、また、堕胎により身体を傷つける女性を一人でも減らすために、正しい避妊の知識を身につけることが大切です。</p>
                <p>女性のみではなく男性も当事者意識を持ち、実践しましょう。</p>
                <h3>受胎のしくみ</h3>
                <p>女性は、生まれたときにはすでに卵子を持って生まれてきます。</p>
                <p>成熟した卵子が1個、体外に排出されることを排卵と呼び、排出された卵子に精子が到達すると卵管の中で受精が起こります。</p>
                <p>受精した卵を受精卵といい、受精卵は約48時間かけて子宮に到達します。<br>
                そして子宮内膜の一部に根を下ろし、さらに7～11日かけて着床といわれる状態を形成します。</p>
                <p>着床したのち、hCG（ヒト絨毛性ゴナドトロピン）というホルモンが分泌されます。</p>
                <p>妊娠検査薬は、このhCGというホルモンを一定値以上検出することで妊娠しているかどうかを判定するもので、着床成立後しばらく経ってからが有効です。</p>
                <p><span>排卵日の前後8日間が妊娠可能時期とされ、可能性が高くなるのは排卵日から3～4日</span>といわれています。</p>
                <p>この間に性交をおこなうと受胎の確率が高い為、不妊治療でもまずおこなわれるのはこのタイミングを計って性交する「タイミング療法」となります。</p>
                <p>妊娠期間は、<span>女性の最終月経の初日を0日目として数え、40週ごろに出産に至ります。</span><br>
                日本では昔から、赤ちゃんはお母さんのおなかの中で「十月十日（とつきとおか）を過ごす」ともいわれてきました。</p>
                <h3>受胎の兆候</h3>
                <p>性行為後、受胎したか否かはどのように見極めるのでしょうか。個人差がありますのであくまでも参考として、受胎の兆候をご紹介します。</p>
                <p>妊娠は、産婦人科にて胎嚢（たいのう）・心拍を測定して確定診断されます。<br>
                卵子と精子が結合した瞬間を妊娠成立と認識されている方もいらっしゃるようですが、実際にはそこから2～3週間経過後、<span>着床を持ってはじめて妊娠状態</span>となります。</p>
                <p>この時期には、何も症状を感じない、兆候がないという女性も多くおられますが、着床時に微量の出血がある「着床出血」を認めるケースもあります。</p>
                <p>また、ちくちくするような下腹部の痛みを感じることもあります。<br>
                おりものが変化する場合もあり、量が増える、サラサラになるなどの変化が現れることもあります。</p>
                <p>ホルモンの影響によると考えられていますが、実際の理由はよくわかっていません。</p>
                <p>他にも熱っぽい、だるいといった風邪様症状が出る方もいらっしゃいます。精神的に不安定になり、イライラしやすくなることもあるようです。</p>
                <p>妊娠初期症状ともいわれる上記の症状が疑われた場合には、タイミングをはかって妊娠検査薬を試してみるのもおすすめです。</p>
