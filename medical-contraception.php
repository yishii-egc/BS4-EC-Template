                <h2 id="medical-03">避妊法を正しく理解する</h2>
                <figure class="section_eyecatch float-right ml-2 mb-2">
                  <img src="images/medical-guide/single/medical-contraception02.jpg" alt="" class="img-fluid img-thumbnail">
                </figure>
                <p>情報があふれかえっている現代において、<span>誤った避妊法や迷信めいた避妊法を正しいと勘違いしている方も多く</span>おられます。</p>
                <p>正しい避妊法をしっかりと理解して実践し、自分とパートナー、将来の家族を守っていきましょう。</p>
                <p>具体的には、コンドームの装着、ピルの服用、殺精子剤の装着、ペッサリーの装着、避妊リングの使用があげられます。</p>
                <p>いずれの方法も100％完全に避妊できる訳ではありませんが、コンドームは避妊効果も高く、性病感染も防ぐ効果もあるのでおすすめの方法です。</p>
                <p>男性の膣外射精や、膣内洗浄、炭酸水洗浄は誤った方法で、避妊効果はありません。</p>
                <p>正しい方法で避妊をおこなうことで、望まない妊娠を回避できる可能性が高まります。</p>
                <h3>性行為の制限</h3>
                <p>100％避妊をしたいと望むならば、当たり前ですが<span>性行為をしないことが一番</span>です。</p>
                <p>しかし、健全な男女がヒトとして生きるうえで、<span>完全な禁欲生活を送ることはかなり難しい</span>でしょう。</p>
                <p>ヒトは、生殖や快楽のためではなく、愛情を確認するために性行為をおこなうことも多い生き物です。</p>
                <p>昨今はスローセックスという言葉も生まれており、これは男女がお互いをいたわりながらおこなう行為の中で、喜びと幸福を感じることが可能であると定義されています。</p>
                <p>考えうる避妊のための工夫の一つとして、挿入を伴わないセックスがあげられます。</p>
                <p>膣内への挿入なしで満足を得られなければ、望まない妊娠を防ぐことが可能です。<br>
                挿入を伴わないセックスとは、オーラルセックスやアナルセックスを指します。</p>
                <p>ただし、粘膜部を介して感染症がうつる可能性も否定できません。</p>
                <p>オーラルセックスやアナルセックスをおこなう際にはシャワーを浴びるなど、身体を清潔にしてからおこなうようご注意ください。</p>
                <p>このように粘膜を介した性感染症に気を付けながら、愛情を持って相手に接し、挿入なしのスローセックスを楽しむことも、避妊に繋がる工夫のひとつとおこなえるでしょう。</p>
