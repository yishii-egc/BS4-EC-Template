                <h3>避妊具</h3>
                <p>避妊具はいくつも種類があります。<br>
                まずは、最も汎用されている避妊具であるコンドームです。</p>
                <p>コンドームは、ラテックスやポリウレタンでできた薄い膜をサック状にしたものです。<br>
                男性が装着するタイプで、膣内に挿入する前に勃起した陰茎に被せますが、<span>避妊の確率を上げるには、勃起直後の装着が推奨</span>されます。</p>
                <p>陰茎の大きさに合わせてさまざまなサイズが用意されていますので、それぞれが体に合ったものを選ぶことが可能です。</p>
                <p>サイズが合わないと挿入中に外れてしまうことがあるので注意が必要です。</p>
                <p>最近は陰茎が大きい方用のサイズも、店頭にはないもののインターネット上ではよく販売されており、入手しやすくなってきました。</p>
                <p>ペッサリーは、もともと子宮の位置を元に戻すために使われていました。<br>
                <span>子宮に被せるような形で挿入</span>しますが、ゼリーなどを併用することが多いようです。</p>
                <p>ヒトによって支給や膣の形状が異なるので、入手するには産婦人科の診察が必要となっています。</p>
                <p>子宮内避妊用具（IUD）は、子宮内にリング状もしくはコイル状の避妊具を設置し、体に備わっている異物排除機能を利用して受精卵の着床を妨げるという仕組みで使われます。</p>
                <p>病院で設置しなければならないうえ、子宮外妊娠は防げないため汎用はされていません。</p>
                <h3>避妊薬</h3>
                <p>経口避妊薬（OC）ピルと呼ばれる治療薬で、女性が服用します。</p>
                <p>ホルモンを調整し、人工的に黄体期（排卵終了期）の状態を維持することによって排卵を停止させる方法です。</p>
                <p>避妊薬ピルには<span>避妊目的だけではなく、生理日の調整や子宮内膜症などの婦人科疾患の治療に使われる</span>こともあります。</p>
                <p>副作用として、血栓症や肥満、むくみ、イライラ、吐き気、膣炎、肝機能障害が現れることもありますので、医療機関への受診が必要です。</p>
                <p>近頃では、身体の負担を軽減した低用量ピル、さらに成分含有量を調整した超低用量ピルが開発され、女性の身体にやさしい避妊薬が多数販売されています。</p>
                <p>喫煙とピルの服用は、心臓や循環器系の副作用が現れる可能性が高まるため、<span>ピルの服用時は禁煙をおすすめ</span>します。</p>
                <p>片頭痛、糖尿病、高血圧の方や35歳以上で1日15本以上タバコを吸う方、腎障害・肝障害の借る方はピルの服用はできません。</p>
                <p>他には、日本では認可されていませんが、プロゲステロンを含んだ徐放性剤（じょうほうざい）と呼ばれるゆっくりと効く治療薬を女性の皮下に埋め込んで、避妊効果を発揮させるインプラント法もあります。</p>
                <p>皮膚パッチ剤として知られる治療薬には、エブラ（避妊パッチ）があります。<br>
                エストロゲンとプロゲステロンを含んだパッチ剤で、1週間の避妊効果が期待できます。</p>
                <p>またプロゲステロンを筋注もしくは皮下注する方法もありますが、こちらは3か月おきに注射する必要があります。</p>
                <h3>緊急避妊</h3>
                <p>避妊の意思があっても、コンドームの破損やピルの飲み忘れが原因で避妊に失敗してしまうことがあります。</p>
                <p>また、心苦しくもレイプなどの暴行事件に巻き込まれることも考えられます。<br>
                こういった場合に有効とされているのが、緊急避妊です。</p>
                <p>女性側が、緊急避妊ピルやアフターピルと呼ばれている治療薬を服用します。</p>
                <p>ピルに含まれるホルモンの働きにより、一時的に子宮内部の状態を変化させ、受精卵が着床しにくい状態にするものです。病院での診察が必須で、<span>72時間以内に服用しなければならないという条件が課されています。</span></p>
                <p>緊急避妊ピルは、副作用として吐き気が出たり、眩暈、頭痛、腹痛、乳房の違和感の症状を認めたりすることがあります。</p>
                <p>あくまでも一時的なもので、24時間以上継続することはないとされています。異常が続く場合には医師の診察を受けるようにしてください。</p>
                <p>緊急避妊よりも、コンドームの装着やピルの服用を事前におこなうほうが避妊できる確率が高いといわれています。</p>
                <p><span>アフターピルの使用は、緊急時における応急処置</span>ととらえ、日頃からの事前避妊を実践してください。</p>
                <h3>不妊手術</h3>
                <p>外科的な治療を望むのであれば、不妊手術という方法もあります。</p>
                <p>卵管または精管を縛り、卵子または精子の移動を食い止めることで避妊をおこなう方法です。</p>
                <p>一般的に、経腟または経腹による手術が必要となりますが、精管を縛る手術は局所麻酔でおこなうことも可能です。男性の手術はパイプカットとも呼ばれています。</p>
                <p>パイプカット手術をしても精液が無くなる訳ではなく、射精も可能です。<br>
                ただし、既婚男性のうち配偶者の同意がある場合のみ許可されています。避妊の効果は非常に高く、再接続手術を受けても回復は難しいので、大きな決意が必要となります。</p>
                <p>女性の場合は、帝王切開手術後や普通分娩による出産の直後におこなわれることもあります。</p>
                <p>未婚で不妊手術を受けている女性は、結婚前に夫となる人に事実を告げる義務を負っています。</p>
                <p>妊娠を望まない夫婦や、妊娠することで母体の命が脅かされたり持病が悪化したりする可能性がある場合、胎児に影響がある場合に手術適応となります。</p>
                <p>子宮の全摘出手術がおこなわれることもありますが、これは子宮内膜症などの病気が重症と判断される場合が多いようです。</p>
                <h3>その他</h3>
                <p>その他の避妊の方法として、基礎体温法やオギノ式と呼ばれる方法をご紹介します。</p>
                <p>基礎体温法は、リズム式とも呼ばれ、女性の月経周期と基礎体温変動から排卵日を予測する方法です。</p>
                <p>一般的に、女性の基礎体温は排卵を境に低温期から高温期へ、生理を境に高温期から低温期へとうつります。このサイクルから排卵日を予測し、<span>妊娠の可能性が高い時期に性交を避けるという避妊方法</span>です。</p>
                <p>排卵日の翌々日以降からは妊娠しにくいといわれていますが、毎日基礎体温をつけていても排卵日を100％予測することは難しいものです。</p>
                <p>ストレスや乱れた食生活が原因で、生理の遅れや排卵日のズレが生じることも多々あります。<br>
                基礎体温法はあくまでも補助的な避妊方法として実践し、他の避妊方法と併用することが望ましいでしょう。</p>
                <p>オギノ式と呼ばれる方法は、生理周期とは関係なく、<span>次の生理予定日から14日目とその前後2日間（計5日間）を妊娠しやすい日と定め、この期間には性交を控えるという方法</span>です。</p>
                <p>精子・卵子の生存期間や生理周期にも個人差がありますので、オギノ式に関してもあくまでも目安として活用してください。</p>
