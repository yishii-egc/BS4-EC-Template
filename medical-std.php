                <h2 id="medical-01">性感染症（STD・STI）とは</h2>
                <p>性感染症とは、性行為やそれに類似する行為をおこなうことで感染する病気のことです。</p>
                <p>以前は「性病」という表現が使われていましたが、現在は「性感染症」という言葉を使うようになっています。</p>
                <p>また性感染症の英文の頭文字をとって「STD」や「STI」と略して呼ばれることもあります。この2つの英語略称は、少し意味合いは異なりますが、日本では同じ性感染症の意味で捉えられています。</p>
                <p>性感染症と一言で書いても、その原因となるウイルスや細菌はいくつかあります。<br>
                性感染症の病態に類似した紛らわしい感染症の症状もあり、病院で診断して適切な治療をしてもらう必要があります。</p>
                <p>性感染症を予防するには、正しい知識を持つ必要があります。<br>
                感染した疑いがある場合には、<span>早期発見と早期治療が肝心</span>となります。</p>
