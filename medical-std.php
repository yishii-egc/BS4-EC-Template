                <h3>パートナーとともに治療する</h3>
                <p>自分が性感染症にかかったということは、かなり高い確率でパートナーも感染している可能性があります。</p>
                <p>性感染症の盲点は、自覚症状がない、痛みをほとんど伴わない症状もあることです。<br>
                自覚症状がないと、自分が性感染症に感染しているとは気づかずに、どんどんと感染源を広げてしまいます。</p>
                <p>自覚症状があっても他の病気や症状と思い込んでしまう、一時的な体調不良で片付けてしまい、同様に感染源を広げることにもなります。</p>
                <p>つまり自覚がないままあなた自身そしてパートナーは、感染源となっている場合があるのです。</p>
                <p>性感染症が発症した場合、重要なことは、<span>自分とパートナー双方で治療する</span>ことです。<br>
                性感染症の場合、いずれか一方のみが治療しても、まったく意味がありません。</p>
                <p>仮に自分1人だけ治療して完治しても、パートナーは感染したままです。<br>
                そのパートナーと性行為すると、再び性感染症に感染してしまいます。</p>
                <p>これを何度も繰り返すことになったら、いつまでも性感染症は完治しません。卓球のラリーでピンポン玉が行ったり来たりする様子から<span>「ピンポン感染」</span>とよばれます。<br>
                このピンポン感染を唯一解消する方法が、自分とパートナーの2人で治療することです。</p>
