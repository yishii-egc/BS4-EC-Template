                <p>性感染症検査や治療で確実なのは、<span>病院で診察をする</span>ことです。</p>
                <p>最近は性感染症専門のクリニックが都内や主要都市を中心に増えています。またプライバシーにも配慮して、気軽に来院できるようにもなっています。<br>
                確実で早い検査や治療を受けるために、性感染症専門のクリニックがおすすめです。</p>
                <p>しかし、地域によっては少ないこともあります。<br>
                性感染症専門の診療科目に代わるものとしては、泌尿器科や皮膚科、婦人科、産婦人科があります。<br>
