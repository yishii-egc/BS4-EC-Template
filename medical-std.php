              <section>
                <h2 id="medical-03">性感染症の種類</h2>
                <figure class="section_eyecatch float-right ml-2 mb-2">
                  <img src="images/medical-guide/single/medical-STD02.png" alt="" class="img-fluid img-thumbnail">
                </figure>
                <p>性感染症の原因は、ウイルスや細菌となります。<br>
                性行為などによってパートナーに感染し、<span>性感染症の種類により症状や発症までの期間や異なります。</span></p>
                <p>ここでは性感染症の代表的な種類や、各症状の特徴などを簡単に紹介します。<br>
                注意点としては、同じ該当する症状であっても、性感染症の種類を決めつけないことです。あくまでも参考程度と考え、検査とおこなった上で治療を始めましょう。</p>
                <p>なぜなら、性感染症ではない感染症の可能性もあるからです。また、違うウイルスや細菌により症状を引き起こしている可能性も考えられます。</p>
                <p>個人の判断で間違った治療をおこなうと完治しないばかりか、症状を進行させるおそれがあります。<br>
                様子がおかしいなと感じたら、<span>早めに症状を特定して早期に治療することが重要</span>です。</p>

                <h3>クラミジア</h3>
                <p>クラミジアは、<span>日本で発症する患者数が一番多い性感染症</span>です。</p>
                <p>クラミジアは自覚症状が少なく、感染していても気づかないケースがあります。<br>
                発症するまで1〜4週間程度かかるため、知らずのうちに感染を増やすリスクもあります。</p>
                <p>症状としては、<span>かゆみや腫れ、排尿時や性行時に軽い痛みが出ます。</span>症状が進行すると精巣や卵巣、子宮の炎症を引き起こし、妊娠時に感染すると胎児にも影響が出て、肺炎などを引き起こします。</p>
                <p>また、粘膜の炎症を引き起こすことで、HIVなどの他の性感染症を誘発する原因にもなるので要注意です。</p>
                <p>ある病院の統計によりますと、全年齢においてクラミジアの感染率がアップしています。<br>
                口を使ったオーラルセックス（口腔性交）によるものも感染率の要因となっており、性器同士の接触だけが原因とは一概にいえなくなっているのが現状です。</p>

                <h3>淋病</h3>
                <p>淋病はクラミジアに次いで発症率の多い性感染症で、性行為の経験者であれば、誰でも感染している可能性がある性感染症です。</p>
                <p><span>淋病に感染した30%の方はクラミジアと併発している</span>との調査結果もあるため、いずれか一方の症状が発症した場合は、<span>双方に検査をするようおすすめします。</span></p>
                <p>淋病の原因となる淋菌は、咽頭や直腸などといった性器以外の粘膜に存在します。<br>
                そのため、オーラルセックス（口腔性交）で感染するリスクも高まります。<br>
                淋病に感染することで不妊の原因にもなるため、将来的に子宝に恵まれたい方は気にかけておくべきです。</p>
                <p>症状としては、男女により違いが出ます。</p>
                <p><span>男性は尿道に灼熱感や激しい痛みが発症したり、排尿時に濃い黄白色の膿が出たりするなどの症状が出ます。</span>
                陰嚢に淋菌が到達すると、歩くのが困難なほどの痛みを引き起こすこともあります。</p>
                <p><span>女性の場合、感染した80%程度は症状を感じることはありません。</span>そのため、知らずのうちに感染を広げるおそれがあります。</p>

                <h3>梅毒</h3>
                <p>性感染症といったら、真っ先に梅毒を思い浮かべる方もいるかもしれません。</p>
                <p>歴史の教科書に「不治の病」として紹介されていたため、昔の病気のイメージを持たれているかもしれませんが、近年、この<span>梅毒も若い方を中心に増えつつある</span>のです。</p>
                <p>昨今おこなわれている調査によると、2011年以降、20代前半の女性を中心に増加傾向にあります。
                また、出産時に胎児に梅毒が感染するケースも増えています。</p>
                <p>梅毒の怖いところは、発症してもしばらくすると症状が落ち着いてしまい、症状が感じることがないままどんどん重症化するところです。</p>
                <p>第1期から第4期にわかれ、第2期までは3週間〜3か月で起こる早期梅毒といわれます。<br>
                <span>性器周辺にしこりや赤い発疹が現れるのが症状の特徴</span>となります。</p>
                <p>第3期と第4期になると、梅毒の症状が全身に現れます。<br>
                麻痺や認知症といった日常生活が困難となり、最悪死に至ってしまうことがある怖い性感染症です。</p>

                <h3>性器ヘルペス</h3>
                <p>性器ヘルペスは、<span>性行為の方法に関わらず感染するリスク</span>があり、特に不特定多数の方と性行為をおこなう場合には注意が必要です。<br>
                一方、夫婦など特定のパートナーでの感染の割合は、10%程度に留まっています。</p>
                <p>感染は粘膜が傷つくことにより感染、そして発症します。</p>
                <p>感染場所は性器や肛門、口やその周辺となり、最初の発症時には、激しい痛みを伴うことがあります。排尿時や歩行時に痛みが出て、日常生活に支障をきたすことがあります。</p>
                <p><span>性器ヘルペスに一度感染すると、完全除去は難しくなります。</span>
                2回目以降の再発は軽い症状で落ち着くのが一般的ですが、体調悪化などで免疫機能が低下すると、性行為の有無に関係なく発症することがあります。</p>


                <h3>尖圭コンジローマ</h3>
                <p>尖圭（せんけい）コンジローマは、性行為の方法に関わらず感染します。<br>
                性行為経験者であれば、一生に一度は発症するともいわれています。</p>
                <p>尖圭コンジローマの特徴は、<span>イボ状の突起が局所的に発生</span>します。<br>
                特に痛みやかゆみなどの自覚症状はなく、突起ができることで感染したことがわかります。</p>
                <p>性行為時にこすれることで傷ができ、その傷口から感染します。<br>
                <span>性行為が盛んな男女、性器や肛門、膣内部に多く発症する</span>のはそのためです。</p>
                <p>尖圭コンジローマはヒトパピローマウイルス（HPV）が原因となります。
                HPVの種類は100以上あり、このうち2種類が尖圭コンジローマを発症させるウイルスに該当します。</p>
                <p>免疫機能が正常に働いている場合、無自覚のまま症状が緩和することがあります。
                逆に免疫機能が低下すると、再発するおそれがあります。</p>

                <h3>トリコモナス</h3>
                <p>トリコモナスは、トリコモナス原虫と呼ばれる微生物により起こる性感染症です。<br>
                男性よりも女性に症状が出やすくなっています。</p>
                <p><span>膣トリコモナス症と呼ぶように、膣内やその周辺の性器で増殖</span>します。</p>
                <p>感染したことに気づく1つの症状は、<span>おりものの悪臭</span>です。
                別の表現では、魚の腐敗臭に類似しているほど、かなりの悪臭が立ち込めます。</p>
                <p>そのため、自分自身だけでなく、その悪臭で性行為時に不快感を持つほどともいわれています。<br>
                他にも、泡状や黄緑色のおりものが大量に出ます。<br>
                また、排尿時や性行為時に痛みを感じることもあります。</p>
                <p>男性は、ほとんど無自覚で知らない間に完治します。<br>
                感染源は性行為だけでなく、<span>他の人が接触したタオルや便座、お風呂から感染する</span>こともあります。</p>
                <p>日本では衛生環境が整っているため、性行為時の感染が主な感染源と考えてよいでしょう。</p>

                <h3>HIV（エイズ）</h3>
                <p>HIV（ヒト免疫不全ウイルス）やエイズは、誰もが知っている有名な性感染症の1つです。</p>
                <p><span>感染源は性行為のほかにも、血液からの感染があります。</span>
                また、女性がHIVに感染している状態で、出産すると子どもに感染するリスクが高まります。</p>
                <p>HIVに感染後2〜6週間に、インフルエンザに似た発熱や頭痛、喉の痛みが出ます。<br>
                その症状はすぐに消えて、5〜10年は何も変化はありませんが、免疫機能はどんどん低下します。</p>
                <p>免疫機能が大幅に低下すると免疫不全による、さまざまな体調不良が現れます。<br>
                免疫不全が原因で起こる、さまざまな病気や症状のことを総称してエイズと呼びます。</p>
                <p>昔は治療が極めて困難な病気の代表格でしたが、<span>現在エイズの治療は確立され、抗HIV薬を飲み続けることで、症状を抑制できる</span>ようになっています。</p>

                <h3>B型肝炎</h3>
                <p>B型肝炎は、各種ある肝炎ウイルスのうち、B型で起こる肝炎で、感染力が強いことが特徴です。<br>
                日本赤十字社の輸血用血液製剤の事件で有名になり、その名称が広く認知されるようになりました。</p>
                <p>B型肝炎の発症は性行為そのものだけでなく、出血を伴う場合にリスクを伴います。<br>
                出血箇所の傷口から、B型肝炎ウイルスを持ったパートナーを介して感染することがあります。</p>
                <p>症状としては、<span>倦怠感を中心に食欲不振や吐き気など</span>を伴います。
                また、肝炎の症状で特徴的な、体が全体的に黄色になる黄疸も発症することがあります。</p>
                <p><span>症状が進行すると、慢性肝炎を引き起こすことが多く、B型肝炎患者の10〜20%が慢性肝炎になるといわれています。</span>
                そのまま放置すると、肝硬変や肝がんの発症のリスクが高まります。</p>

                <h3>C型肝炎</h3>
                <p>C型肝炎は、B型肝炎と同様に代表的な肝炎の1つです。</p>
                <p>症状としては倦怠感や食欲不振が現れ、症状も軽いのが特徴です。<br>
                そのため、単なる体調不良と見なされ、C型肝炎と気づかずに症状を進行させてしまいます。</p>
                <p>各種ある肝炎ウイルスのうち、C型肝炎は、出血を伴う性行為のときに感染のリスクが高まります。
                つまり、<span>
                傷口を介して肝炎ウイルスに感染するのがほとんど</span>となります。</p>
                <p>特に女性の場合には、生理中の出血がある場合には、感染のリスクが高まります。<br>
                また、不特定多数の人と性行為をすると、感染のリスクが高まります。</p>
                <p>C型肝炎はB型肝炎と比べて、肝炎が慢性化しやすくなり、慢性肝炎を放置すると、肝硬変や肝がんを発症しやすくなりますので、早期治療が必須です。</p>

                <h3>軟性下疳（なんせいげかん）</h3>
                <p>軟性下疳は、<span>東南アジアやアフリカの熱帯地域や亜熱帯地域で、多く見られる性感染症</span>です。</p>
                <p>そのため他の性感染症や性感染症と違い、あまり聞き慣れないかもしれません。<br>
                日本において発症例は少なく、1945〜1950年の終戦頃にときどき見られたくらいです。</p>
                <p>原因となるのは、軟性下疳菌と呼ばれる細菌の一種です。<br>
                感染すると数日で性器や肛門付近に、痛みを伴う水疱や水ぶくれができて、やがて潰れます。</p>
                <p>注意したいのは、水疱が潰れた時に傷口ができることです。<br>
                性行為などによって傷口から、さらに別の性感染症を引き起こすウイルスや細菌に感染するリスクが高まる危険性があります。</p>
                <p>実際に軟性下疳によって、HIVの感染率が高まったケースもあります。<br>
                特に海外の旅行先で、軟性下疳の発症が多いとされる現地の方と性行為した場合には、感染の可能性も念頭におくべきでしょう。</p>

                <h3>カンジダ</h3>
                <p>カンジダは性器周辺で発症する、<span>真菌の一種であるカビが原因の症状</span>です。<br>
                厳密には性感染症ではないものの、性感染症の一種としてあつかわれることもあります。</p>
                <p>カンジダは常在菌の一種であり誰でも所有しています。皮膚や腸管、女性の膣内に存在し、免疫機能によって増殖が抑えられています。</p>
                <p>しかし、ストレスや体調悪化により、体内の環境バランスが崩れることで、カンジダが増殖します。<br>
                <span>特に女性に発症しやすく、膣周辺のかゆみや腫れ、排尿時や性行為時の痛み、白いチーズ状のおりものがたくさん出ます。</span></p>
                <p>男性は感染しても自覚症状が現れにくく、気づかないまま完治するケースもあります。<br>
                レアなケースとして亀頭炎や尿道炎により、痛みや腫れを発症することもあります。</p>

                <h3>いんきんたむし</h3>
                <p>いんきんたむしは、<span>白カビの一種である白癬菌によって起こる症状</span>です。
                性感染症に関係なく誰でも発症し、性感染症ではありません。</p>
                <p>白癬菌が<span>性器周辺や内股に発症した場合、いんきんたむしと呼びます。</span>
                同じ白癬菌でも頭部に発症すると、しらくもと呼び、足であれば水虫と呼びます。</p>
                <p>白癬菌は高温多湿な環境を好み、性器周辺は適した環境となります。
                汗をかきやすい人、性器周辺を不潔にしている場合、発症するリスクが高まります。
                特に男性の陰嚢部分は、肌に密着しやすく通気性が悪くなるため注意が必要です。</p>
                <p>症状としては、皮膚が赤く腫れる、強いかゆみを通り越して痛みが出ることもあります。
                いんきんたむしに似た性感染症もあるため、病院で検査してもらうことが重要です。</p>
              </section>

