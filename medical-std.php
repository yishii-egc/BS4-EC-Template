                性行為後に口や咽頭に違和感を持った場合には、耳鼻咽頭科でも診察してもらうことも可能です。</p>
                <p>病院で診察を受ける場合には、性行為と症状について、しっかり伝えましょう。<br>
                具体的に伝えることは、性行為に関しては、「いつ、どこで、誰と」を伝えます。</p>
                <p>症状は「いつ頃から、どのような」症状が出ているのかを伝えます。<br>
                検査の種類や料金は、性感染症の種類や可能な検査内容により大きく変わります。</p>
                <p>診察や薬の処方の治療に関しては、基本的に保険が適用されます。
                検査に関しては、ケースにより保険適用の有無が変わりますので、確認することをおすすめします。</p>

                <h3>保健所へ相談</h3>
                <p>病院に行くのに抵抗がある場合、<span>各自治体にある保健所で相談や検査する</span>ことも視野に入れましょう。</p>
                <p>地元の保健所を探す、性感染症検査ができる項目や検査可能な時間は、ネットで簡単に検索できます。お住まいの都道府県や市区町村で検索するのが、最も確実な方法でしょう。</p>
                <p>多くの保健所では、無料で性感染症検査が可能です。<br>
                予約の有無に関しては、保健所によって変わりますので確認をしましょう。<br>
                予約の場合には、定員が決められていることもあるため、余裕を持って予約することをおすすめします。</p>
                <p>即日で検査結果がわかるものや対応可能な保健所の違いもあるため、事前に確認をおこないましょう。</p>
                <p>保健所のメリットは、性感染症検査が基本的に無料で、基本的に匿名で検査を受けることが可能です。</p>
                <p>ただし、デメリットとしては、検査日時が限られているところがあることです。性感染症検査できる項目も病院と比べて少なくなります。<br>
                そして病院ではないため、治療をおこなうことはできません。</p>
                <p>このデメリットを知ったうえで、性感染症検査が基本無料であるメリットを活かし、気軽に相談するのもおすすめです。</p>

                <h3>セルフキットの利用</h3>
                <p>病院や保健所に行って検査するのに抵抗がある場合、セルフキットがおすすめです。</p>
                <p>セルフキットは<span>自宅に検査用の道具を郵送してもらう方法</span>です。いろいろな種類の検査キットを通販で気軽に購入することも可能となっています。</p>
                <p>セルフキットは、病院に返送するもの、自宅でその場で判断できるものがあります。</p>
                <p>最近では性感染症の専門クリニックでも、検体を採取後に返送するタイプのセルフキットも用意されています。単体の性感染症検査だけでなく、複数の種類が検査できるセット販売もされています。</p>
                <p>検査の方法は、性感染症の種類やセルフキットにより方法が異なります。</p>
                <p>主に喉の粘膜、血液、尿、尿道や膣内など採取するのが一般的です。<br>
                説明書に従い、正しく採取をおこないましょう。</p>
                <p>病院に返送するタイプは、成分が劣化しないうちに早めに返送をおこないましょう。</p>
                <p>自宅でその場で検査できるタイプは、所定時間後に出た結果を自分で判定します。<br>
                セルフキットは手軽にできる分、正しい検査結果が出ないこともあります。</p>
                <p>間違った手順や準備をおこなった場合、検査結果に影響が出ることもあるため注意しましょう。<br>
                また、セルフキットそのものの品質により変わることもあります。<br>
                なるべく信頼のできるもの、新しい検査キットを手に入れるようにしましょう。</p>
