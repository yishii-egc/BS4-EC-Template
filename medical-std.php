              <section>
                <h2 id="medical-05">性感染症を予防する方法</h2>
                <figure class="section_eyecatch float-right ml-2 mb-2">
                  <img src="images/medical-guide/single/medical-STD04.jpg" alt="" class="img-fluid img-thumbnail">
                </figure>
                <p>性感染症は正しい知識や適切な対応方法を持つことで、感染リスクを大幅に減らすことが可能です。<br>
                つまり、感染してからの治療ではなく、感染しない、感染させないための事前予防がとても重要なのです。</p>

                <p>性感染症は自分だけの問題ではなく、パートナーにも大きく関わる問題です。<br>
                <span>自分のことだけでないからこそ、性感染症の予防を真剣に考えるべき</span>ことといえるでしょう。</p>
                <p>一番の予防は、性感染症のリスクから完全に離れることです。<br>
                そうすれば、自分の不注意により性感染症を引き起こすことは、ほぼなくなるでしょう。</p>
                <p>とはいえ性行為そのものを避けることは、難しいのも実情です。<br>
                性欲は三大欲求の1つでもあり、また性行為は子孫繁栄のための大事な行為です。</p>
                <p>それではどのような方法で性感染症を予防できるのでしょうか。その方法について検証してみたいと思います。</p>

                <h3>性行為をしない</h3>
                <p>極端な話ですが、<span>性行為をしないことも選択肢のひとつ</span>です。<br>
                性行為をしなければ、必然的に性感染症のリスクを大幅に予防できます。<br>
                基本的に接触することがなければ、問題は起きないからです。</p>
                <p>ここで気になるところとしては、性行為とそうではない境目ではないかと思います。</p>
                <p>一般的な性行為といったら、男女間の性器同士の接触です。
                しかし、今はいろんな方法があり、一般的な方法と違うものまであります。</p>
                <p>手をつなぐ行為に関しては、よほどのことがないかぎり、感染することはないでしょう。<br>
                ただし、精液などがついている場合には、感染する可能性はゼロではありません。</p>
                <p>キスはどうなのかといったら、手をつなぐ行為と比べて感染する可能性があります。<br>
                なぜなら、性感染症の原因となるウイルスや細菌は、口や喉などの粘膜に存在することもあるからです。</p>
                <p>口や舌をつかって性器と接触する行為、いわゆるオーラルセックスをした場合、性感染症になるリスクは十分にあります。口腔内に広がった感染菌をもって、キスをすると感染確率があがることはおわかりになるかと思います。</p>
                <p>最近増加傾向にあるクラミジアは、咽頭に感染するのもそれらが原因となっています。<br>
                性器だけでなく、肛門を使った性行為に関しても、わずかな傷口から菌がうつるとされているため注意が必要です。</p>

                <h3>性行為を安全におこなう</h3>
                <p>性感染症が増える原因の1つとして、複数のパートナーと性行為をすることです。パートナーが増えれば増えるほど、性感染症のリスクは増加します。</p>
                <p><span>一番安全なのは、お互いパートナーの他に性行為をする人がいないことです。</span><br>
                性行為をするパートナーを限定すること、当たり前のようでとても重要なことといえます。</p>
                <p>そもそも性行為がなければ、性感染症に感染していることは非常にまれです。<br>
                しかし、昔に比べて性行為に関する情報が増え、自由になっている今は難しいかもしれません。</p>
                <p>性行為が初めてといわれても、それが真実か否かの証明はなかなかできないのも事実です。</p>
                <p>性感染症の危険性のあるパートナーは、風俗関係などで不特定多数の方と性行為を楽しんでいる方となります。</p>
                <p>当然ですが、<span>多くの性交渉を持っている方は、性感染症を持っている方と性行為している可能性が非常に高くなります。</span></p>
                <p>性行為を目的とした付き合いの場合、お互いが開放的になりがちです。将来的に結婚をして子どもを持つことを少しでも考えるのであれば、快楽ばかりにとらわれず、性感染症のリスクは回避するべきです。</p>
                <p>若気の至り、一時的な遊びで後々悔やむことがないよう、性行為をするパートナーをしっかりと見極める、<span>望まない妊娠とともに性感染症のリスクも抑える</span>ことが大切です。</p>

                <h3>より安全な性行為を心掛ける</h3>
                <p>性感染症に感染しないためには、性行為そのものをしないことは確かに有効な方法です。<br>
                100%ではありませんが、感染するリスクは大幅に減少します。</p>
                <p>しかしながら、性行為を一切しないことは、正直なところ難しいでしょう。<br>
                やはり性欲は三大欲求の1つですから、それを抑えるのも人によってはかなり難しいはずです。</p>
                <p>そこで重要なことは、<span>少しでも安全な性行為を心がけること</span>です。<br>
                一例としては、<span>コンドームを着用する</span>ことです。<br>
                コンドームをすれば、性器同士に直接接触する確率が減り、性感染症を抑える効果が期待できます。</p>
                <p>ただし、必ずしもコンドームで性感染症を予防できるとはかぎりません。<br>
                コンドームをしていても、それ以外の肌同士の接触から、感染することもあるからです。</p>
                <p>クラミジアの場合、性器だけでなく口や咽頭の中にも存在するからです。<br>
                口を使ったオーラルセックスをする場合、感染するリスクが高まります。最近では、オーラルセックス用のコンドームも用意されています。</p>
                <p>他にもコンドームを正しく使うことです。<br>
