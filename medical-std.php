              <section>
                <h2 id="medical-02">性感染症の症状</h2>
                <figure class="section_eyecatch float-right ml-2 mb-2">
                  <img src="images/medical-guide/single/medical-STD01.jpg" alt="" class="img-fluid img-thumbnail">
                </figure>
                <p>性感染症になると、その原因となるウイルスや細菌により、さまざまな症状が現れます。<br>
                なかには、感染してから発症するまで数日や数か月、長いものですと数年以上かかるものもあります。</p>
                <p><span>性感染症が発症すると多くの場合、性器周辺に何らかの症状が現れます。</span>
                同じ性感染症でも、性器の形状により男女で違いが出ることもあります。</p>
                <p>特に性行為をしたあとに、<span>性器やその周辺に腫れや痛み、かゆみなどが出た場合には要注意</span>です。<br>
                性器周辺とは遠く離れた部位であっても、同様に気になる症状がある場合には注意をしましょう。</p>
                <p>性感染症と思っても、実は違う感染症や他の症状の可能性も十分に考えられます。
                性行為の有無に関係なく違和感がありましたら、早めに病院で診察を受けたり、検査をしたりして、早く適切な治療を始めましょう。</p>
                <h3>性器周辺の違和感</h3>
                <p><span>性感染症の症状として、よくあるのが性器周辺の違和感です。</span></p>
                <p>主に性感染症の原因となる、ウイルスや細菌による作用によるものと考えられ、その種類や作用により症状や違和感の程度が変わります。</p>
                <p>まずは性器周辺のかゆみや痛みがあります。<br>
                これは、ほぼすべての性感染症に当てはまる症状ですが、具体的には、トリコモナスやカンジダ、
                性器ヘルペスなどがその例です。</p>
                <p>トリコモナスは微生物の原虫の一種で、女性に比較的かかりやすいのが特徴の性感染症となります。</p>
                <p>カンジダはカビの一種で、体内にある常在菌からなるもので、性行為だけでなく免疫機能の低下により発症します。</p>
                <p>性器ヘルペスは、傷口から侵入し一度感染すると除去が難しいウイルスです。他にある症状としては、排尿痛があります。</p>
                <p>性感染症に感染すると、ウイルスや細菌が亀頭や膣周辺、尿道に炎症を起こします。</p>
                <p>この炎症による、腫れや水疱、膿などにより排尿痛を引き起こします。さらに尿道の奥に進行すると、陰嚢に炎症を起こすことがあるので侮れません。<br>
                主に淋病やクラミジア、性器ヘルペスの原因となる症状です。</p>
                <p>性器周辺から少し離れて、太ももの付け根あたりの腫れを引き起こすこともあります。<br>
                リンパ節の腫れが主な原因となり、発症する性感染症は梅毒や性器ヘルペスになります。</p>
                <h3>全身の気になる症状</h3>
                <p>性感染症の症状は、必ずしも性器周辺だけとはかぎりません。<br>
                性器に近い肛門でも症状が現れます。</p>
                <p>該当する性感染症としては、尖圭（せんけい）コンジローマです。<br>
                ウイルスによるもので、<span>突起状のイボが局所的に発症</span>します。痛みを伴わない場合がほとんどです。</p>
                <p><span>唇周辺にしこりや腫れ</span>などが出ることもあります。<br>
                該当する性感染症としては、梅毒があります。</p>
                <p>特に口を使った性行為である、オーラルセックスをおこなう場合に感染します。
                逆に口から性器に感染するリスクも高くなります。</p>
                <p>また、<span>喉の粘膜に感染して風邪に似たような喉の痛みが出る</span>こともあります。<br>
                淋病やクラミジアがそれに該当します。</p>
                <p>つまり<span>性器周辺だけでなく、粘膜全般にも感染源を広げる</span>のです。</p>
                <p>性器を含めて全身に発症するものとしては、梅毒やエイズがあります。<br>
                梅毒は過去の性感染症のイメージが強いかもしれませんが、現在は増えているのが実情です。</p>
                <p>感染すると、<span>性器や口周辺に痛みを伴わない、しこり</span>ができます。感染してから数週間で症状は落ち着きますが、治療をしないと無自覚のまま症状が悪化する非常に厄介な性感染症となります。</p>
                <p>エイズは、HIVウイルスによって、免疫機能が完全に低下してしまう症状です。<br>
                数年の潜伏期間を経過してから倦怠感を伴う、下痢や発熱、その他さまざまな症状や病気を引き起こします。輸血などでも感染する病気です。</p>
                <p>これらのケースにより、<span>性感染症の発症部位は、性器周辺のみでない</span>ことがわかります。</p>
                <h3>女性はおりもの変化にも注意</h3>
                <p>性感染症の症状は男女に共通するものが多くあります。<br>
                一方で男性にはない<span>女性特有の症状</span>もあります。それが、<span>おりものの変化などによるもの</span>です。</p>
                <p>膣内は常在菌のほかにも、性感染症の原因となるウイルスや細菌にとっても、格好の場所となります。</p>
                <p>カッティングチーズや酒粕のような、おりものが出た場合には、カンジダの可能性があります。</p>
                <p>カンジダはもともと膣内にいる常在菌の1つです。性行為だけの感染とはかぎらず、体調不良や免疫機能の低下により、カンジダが増殖することもあります。</p>
                <p>カンジダの増殖に伴って、<span>外陰部の腫れやかゆみも発症</span>します。</p>
                <p>トリコモナスに感染した場合、泡状のおりものが出るようになります。魚が腐ったような強い悪臭を放つのも大きな特徴です。</p>
                <p>また、いつもよりおりものの量が増えた場合には、クラミジアや淋病の可能性が考えられます。<br>
                クラミジアに感染すると、おりものの量が増えるだけでなく、下腹部の痛みを伴います。<br>
                淋病の場合には、痛みを伴うことはありません。</p>
                <p>このクラミジアと淋病に感染した場合、自覚症状を感じないこともあります。
                下腹部の痛みは性感染症の発症と疑うことなく放置してしまいがちで、適切な治療をしないまま症状が悪化してしまうこともあります。</p>
                <p>知らない間に感染源を広げるおそれもあるため、症状らしきものを無関心なまま放置しないようにすることが大切です。</p>
