                <h3>完治するまで治療する</h3>
                <p>性感染症に感染したら次にすることは、<span>完治するまでしっかり治療する</span>ことです。<br>
                これは自分だけでなく、パートナーも同様です。</p>
                <p>性感染症は自覚症状を伴わないことがよくあります。<br>
                一見、症状が改善したとしても、性感染症のもととなるウイルスや細菌は体内にいるケースもあります。</p>
                <p>つまり、完治したのではなく、治療の効果で活動を抑えられている状態だけになります。<br>
                実際に完治したかどうかの確認したい場合は、病院で検査をするかセルフキットを用いて自ら検査するしか方法はありません。</p>
                <p><span>自分の判断で治療をやめてしまうと、再発するリスクが高まります。</span></p>
                <p>ある病院の統計によると、半年以内で再発した方の大半は、勝手に治療をやめた場合です。<br>
                再発してしまうと、今まで治療にかけていた費用や時間がムダになってしまいます。<br>
                そして、結果として自分だけでなく、パートナーに感染源を広げることにもなります。</p>
                <p>性感染症は種類により、治療方法や治療にかかる時間が大きく異なります。<br>
                性感染症によっては、一度感染すると再発を繰り返すものもあります。<br>
                また、慢性化して他の病気や症状を誘発することもあります。</p>
                <p>そのようなことを少しでも抑えるためにも、完治するまで治療する必要があります。</p>

                <h3>コンドーム利用率の低下</h3>
                <p>コンドームと聞くと、避妊のために使用するもの、と思われたかもしれません。<br>
                実は性感染症を予防するのに、コンドームは必要不可欠で、<span>98％の確率で感染を防ぐ</span>とされています。</p>
                <p>そんな予防対策があるにもかかわらず性感染症は増加傾向にあります。<br>
                その理由としては、コンドーム利用率の低下があげられます。</p>
                <p>統計情報によると、日本においてコンドームの出荷数のピークは1993年の6.3億個です。<br>
                1993年のピーク時には、エイズが世界的に流行し、日本でも1992年に大きく感染率を増やしました。</p>
                <p>ところがエイズの驚異が忘れ去られるに連れ、年々コンドームの出荷数が減少傾向にあります。<br>
                2005年にはピーク時の半分の3.2億個と大きく減少しています。<br>
                そして、コンドームの出荷数の減少と反比例して、HIV感染者、クラミジア、淋病が増加しています。</p>
                <p>日本でコンドーム利用率が低下した理由としては、コンドームを使用しないことで快楽を得たいという欲求が抑えられない方が増えたということがいえます。</p>
                <p>性感染症と望まない妊娠のリスクを、誤った知識、自分たちの都合のいい情報でかき消してしまい、性行為に興じる方が増えているということです。</p>
