                <h2 id="medical-04">性感染症は早期発見・早期治療が肝心</h2>
                <figure class="section_eyecatch float-right ml-2 mb-2">
                  <img src="images/medical-guide/single/medical-STD03.jpg" alt="" class="img-fluid img-thumbnail">
                </figure>
                <p>性感染症が蔓延している理由として、性感染症に関する知識不足があげられます。</p>
                <p>出会い系サイトやSNSなどの普及により、異性の相手と遊び感覚で気軽に会えるようになりました。そして性感染症のリスクや知識のないまま、性行為に至って感染するケースも増えています。</p>
                <p>性に関する情報は昔に比べて、ネットを通して簡単に得られるようになっています。<br>
                しかしながら、多くの人は他人事のように思っているのが現状です。</p>
                <p>性感染症は放置すると、症状を進行させるだけではありません。<br>
                性感染症を移された側から、今度は移す側になりかねないからです。</p>
                <p>それゆえに、<span>性感染症や性行為に対する正しい情報やリスクを知ることが重要</span>です。</p>
                <p>感染した、またはその疑いがある場合には、蔓延を防止するため、適切な薬を服用するなど早期治療の判断が必要です。</p>

                <h3>正しい情報を知る</h3>
                <p>日本は先進国の中において、<span>性感染症は増加傾向</span>にあります。</p>
                <p>性感染症についての知識が不足し、当事者意識が薄らいでいでおり、性行為をおこなっていても感染の危険性を持たずにいることがそもそも問題なのです。</p>
                <p>エイズが世界的に流行した1990年代は特効薬もなく、不治の病ともいわれました。<br>
                日本でもキャンペーンがおこなわれ、その成果もあり性感染症の患者数は減少しました。<br>
                しかし、時間の経過とともにエイズや性感染症の危険性が忘れ去られ、再び者数は増加する結果となりました。</p>
                <p>また、性に関する情報や産業も増えたのも、誤った情報を知る原因にもなっています。</p>
                <p>性行為は子どもを作る行為よりも、快楽や遊び感覚で気軽におこなうようになっています。正しい情報を持たずに気軽な感覚で、<span>不特定多数の人と性行為をすることで、性感染症を増やす</span>ことになります。</p>
                <p>性感染症の中には、痛みや不快感を伴わない、自覚症状の現れないものもあります。<br>
                そのため、自分が感染していることを知らずに、周りに性感染症を感染させる原因にもなります。</p>
                <p>自分が性感染症であることに気がついていても、「病気の発症＝性行為」に直結することで、恥じらい隠しとおすことは、パートナーにとっても自分にとってもけしてよいことではありません。</p>
                <p>正しい情報を知り、発症いた場合には直ちに治療することが重要です。</p>
