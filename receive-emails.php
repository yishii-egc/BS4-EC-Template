              <h3 class="main_h3" id="guide02_01">迷惑メール設定されている</h3>

              <p>ご登録メールの受信設定で<span>当サイトからのメールが拒否設定されている</span>、もしくは<span>「迷惑メールフォルダ」に自動で振り分けられている</span>可能性があります。「迷惑メールフォルダ」内をご確認ください。</p>
              <p>「迷惑メールフォルダ」に入っていた場合、当サイトのドメイン「okusuri-shop.com」からのメールを受信許可するよう「迷惑メール設定」の変更をお願いいたします。</p>
              <p>ご利用されているメールサービスによっては、自動で「迷惑メールフォルダ」に移動およびメール削除されてしまうケースもございます。</p>
              <p>当サイトからのメールを全く受信していない場合も<span>「okusuri-shop.com」が「迷惑メール設定」の受信拒否に該当していないか、ご確認いただきますようお願いいたします。</span></p>

