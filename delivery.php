              <h2 class="main_h2" id="guide06">通関手続きについて（関税）</h2>
              <p>通関とは、輸入者が税関に対して輸入申告を行い、所定の審査・検査を経て（必要に応じ、関税・消費税等を納付し）輸入許可を受けるまでの一連の手続きを指し、個人輸入に関しても必要な手続きとなります。</p>
              <dl>
                <dt class="d-inline-block">税関ホームページ</dt>
                <dd class="d-inline-block">〔<a href="http://www.customs.go.jp/zeikan/pamphlet/kojinyunyu/" target="_blank" rel="nofollow">個人輸入 通関案内</a>〕</dd>
              </dl>
              <p>医薬品等の個人輸入は、ほとんどの場合が関税不要でそのまま通関手続きを完了しますが、<span>ごく稀にご本人負担による課税がかかる場合があります。</span></p>
              <p>関税が課税される場合は、「国際郵便物課税通知書」がお荷物とともに通知され、課税額をお支払いになることでお荷物を受け取ることができます。<br>
              （課税額は合計1万円以上の場合で、およそ1000円前後が目安となります。）</p>
              <p>個人輸入の規定数量制限を超過した場合や、個人輸入の範疇を超えるものと判断された場合、お荷物は税関の差し押さえ対象となります。ご注文時の数量超過等にはご注意ください。</p>
              <p>当サイトでは、差し押さえ対象となる輸入禁止の医薬品、偽造品等は一切取り扱っておりませんのでご安心ください。</p>

