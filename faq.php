          </header>
          <div class="guide_main">
            <dl class="faq">
              <dt><span>Q1</span>処方箋は必要ですか？</dt>
              <dd>
                <p><span>A</span>必要ありません。<br>
                当サイトでご用意している商品は、処方箋を必要とせず、ご購入できるものを扱っております。<br>
                病院で処方されている薬を安価なジェネリックでご注文される方が多くいらっしゃいます。</p>
              </dd>

              <dt><span>Q2</span>1度に注文できる量はどれ位ですか？</dt>
              <dd>
                <p><span>A</span>薬機法（旧薬事法）により定められた規定量の範囲内でご注文いただけます。</p>
                <p>購入する商品によって、注文できる量が薬事法により定められております。<br>
                1つの商品に対して数量を守れば、異なる商品を複数注文することは可能です。<br>
                注文できる量は、商品によって異なりますので、下記をご参照ください。</p>
                <ol>
                  <li>外用剤・化粧品・・・標準サイズ1品目24個以内</li>
                  <li>毒薬・劇薬・処方せん薬・・・用法用量からみて1ヶ月分以内</li>
                  <li>医薬品・医薬部外品・・・用法用量からみて2か月分以内</li>
                </ol>
              </dd>

              <dt><span>Q3</span>注文した内容を確認したい。</dt>
              <dd>
                <p><span>A</span>「マイメニュー」⇒「注文履歴」の手順で注文内容をご確認いただけます。</p>
              </dd>

              <dt><span>Q4</span>医薬品の個人輸入は違法ではないですか？</dt>
              <dd>
                <p><span>A</span>違法ではありません。合法です。<br>
                購入者自身が個人的な使用のみ、医薬品の個人輸入は薬機法（旧薬事法）にて認められております。</p>
              </dd>

              <dt><span>Q5</span>海外医薬品通販の輸入手続きは必要ですか？</dt>
              <dd>
                <p><span>A</span>必要ありません。<br>
                個人輸入の手続きは、当サイトがすべて代行いたします。<br>
                国内通販サイトと同じようにカートにいれてご購入していただければ大丈夫です。<br>
                また通関手続きでの手数料は、一切ご請求しておりません。</p>
              </dd>

              <dt><span>Q6</span>税関が通らないことはありますか？</dt>
              <dd>
                <p><span>A</span>ごく稀ですが、あります。<br>
                税関より通知のハガキが届きますので「受取拒否のため返送」としてご返送お願いいたします。<br>
                ご注文を個人名でしたにもかかわらず、税関が通過できなかった場合は、当サイトにて再配達の送料負担をさせていただきます。</p>
              </dd>

              <dt><span>Q7</span>どれくらいで届きますか？</dt>
              <dd>
                <p><span>A</span>商品の発送元によって異なりますが、およそ5～14日を目安でお願いいたします。<br>
                土日祝日、税関の混み状況により遅れが発生する場合があります。<br>
                ご入金確認後、15日を経過しても商品が届かない場合は、お手数ではございますがお客様サポートデスクまでご連絡いただきますようお願いいたします。</p>
              </dd>

              <dt><span>Q8</span>荷物の状況を知りたいです。</dt>
              <dd>
                <p><span>A</span>発送完了の際に、国際書留郵便で発行された追跡番号を、メールにてご連絡させていただきます。<br>
                商品により配送業者が異なる場合もありますが、それぞれの追跡番号が発行されます。<br>
                発送された商品は、当サイトの配送番号窓口もしくは、郵便局の追跡窓口で荷物の状況を確認できます。</p>
               </dd>

              <dt><span>Q9</span>日付や時間指定はできますか？</dt>
              <dd>
                <p><span>A</span>できません。<br>
                原則として国際書留郵便での配送となりますので、日付・時間指定は対応しておりません。</p>
              </dd>

              <dt><span>Q10</span>会社宛てに配送してもらうことはできますか？</dt>
              <dd>
                <p><span>A</span>会社宛へ出荷はできますが、推奨はしておりません。<br>
                ご自宅以外への出荷は、税関検査のチェックが厳しくなり、通過できないことがあります。<br>
                その際は、税関より通知がありますので、お客様自身でご対応ください。</p>
              </dd>

              <dt><span>Q11</span>郵便局留めはできますか？</dt>
              <dd>
                <p><span>A</span>できます。<br>
                注文の際に、受け取りたい郵便番号・住所・郵便局名を記載してください。<br>
                郵便局留めをすることにより、ご家族にも知られずに商品を受け取ることができます。<br>
                商品を受け取る際は、ご本人確認ができる書類（免許証、パスポート等）が必要です。</p>
                <p>郵便局留めの保管期間は30日間となり、期日を過ぎても受け取られない際は、返送されます。<br>
                返送の再配達の送料は、お客様のご負担となりますのでご注意ください。</p>
              </dd>

              <dt><span>Q12</span>代金引換はできますか？</dt>
              <dd>
                <p><span>A</span>海外発送のため、できません。<br>
                弊社にてお支払い完了の確認後、発送を手配させていただきます。</p>
              </dd>

              <dt><span>Q13</span>送料は地域や注文数量で変わりますか？</dt>
              <dd>
                <p><span>A</span>地域や注文数量で変動はしません。<br>
                送料は、全国一律1,000円となります。 商品を1万円以上お買い上げのお客様は、送料を無料とさせていただきます。</p>
              </dd>

              <dt><span>Q14</span>会員登録や注文完了のメール通知が来ません。</dt>
              <dd>
                <p><span>A</span>迷惑メールフォルダに振り分けられているか、迷惑メール設定をされていないかご確認をお願いいたします。<br>
                当サイト内でログインしていただくことにより、メール内容をご確認することもできます。<br>
                メールアドレスにお間違いがない場合は、メール不着・携帯電話の設定をご確認お願いいたします。</p>
              </dd>

              <dt><span>Q15</span>注文した商品と違ったり、商品が破損していた場合はどうなりますか？</dt>
              <dd>
                <p><span>A</span>早急にお客様サポートデスクまでご連絡いただきますようお願いいたします。<br>
                すぐに弊社負担にて良品を再発送させていただきます。<br>
                到着後、7日以上経過いたしますと補償対象外となりますので、お早めにご連絡をお願いいたします。</p>
              </dd>

              <dt><span>Q16</span>男性名義で避妊薬ピル、女性名義でED治療薬を購入することはできますか？</dt>
              <dd>
                <p><span>A</span>できません。<br>
                  薬機法（旧薬事法）にて医薬品の個人輸入は、使用者本人のみ購入可能となっておりますので、必ず使用者ご自身でご購入ください。
                </p>
              </dd>

              <dt><span>Q17</span>友人や家族の分を注文したり、商品をプレゼントしてもいいですか？</dt>
              <dd>
                <p><span>A</span>友人・家族を含む第三者への転売や譲渡は薬機法（旧薬事法）にて認められておりません。</p>
              </dd>

              <dt><span>Q18</span>商品の返品はできますか？</dt>
              <dd>
                <p><span>A</span>できません。<br>
                弊社は販売業ではなく、個人輸入代行業です。<br>
                個人輸入の性質上、商品が未開封状態であっても、返品は一切お受けできません。</p>
              </dd>

              <dt><span>Q19</span>領収書を発行してほしい。</dt>
              <dd>
                <p><span>A</span>「マイメニュー」⇒「注文履歴」⇒「領収書の発行」の手順で発行できます。</p>
              </dd>

              <dt><span>Q20</span>会員情報を変更したい。</dt>
              <dd>
                <p><span>A</span>「マイメニュー」⇒「登録情報」にて変更できます。</p>
              </dd>

              <dt><span>Q21</span>メールマガジンを止めたい。</dt>
              <dd>
                <p><span>A</span>メールマガジンの停止は、「<a href="contact.html">お問い合わせ</a>」よりご連絡ください。</p>
              </dd>

              <dt><span>Q22</span>退会手続きの仕方を知りたい。</dt>
              <dd>
                <p><span>A</span>会員退会をご希望の方は、会員様ご本人がお客様サポートデスクまで退会したい旨をご連絡ください。<br>
                退会申請後10日以内に退会処理を行います。</p>
              </dd>

              <dt><span>Q23</span>不明な点を質問したい場合は、どうすればいいですか？</dt>
              <dd>
                <p><span>A</span>お気軽に「<a href="contact.html">お問い合せ</a>」よりご質問ください。</p>
              </dd>
