              <section>
                <h2 id="medical-03">原因</h2>
                <h3>器質性</h3>
                <p>年齢と共に多く発症するのがこのタイプです。<br>
                  血液の循環が悪くなる動脈硬化は、EDとの関連性も深く、主な原因といえます。</p>
                  <p>正常な勃起は、性的刺激があった時に脳からのシグナルをうけ、神経を仲介して陰茎に伝達され、陰茎海綿体の動脈を拡張し血液が流れ込むことによって成立します。動脈硬化で血管が拡がらない、血流が悪くなるといった状態なると、結果的に勃起しづらくなるというわけです。</p>
                  <p>糖尿病や高血圧など生活習慣病の方がEDを引き起こしやすいのも、それらの症状が血管に負担をかけているからと考えらます。血管以外にも神経にかかわる病気（脳出血、脳外傷、アルツハイマー病など）や泌尿器系の病気（前立腺肥大症など）を患っていても、EDになりやすい状況に陥りがちです。</p>

                <h3>心因性</h3>
                <p>EDはカラダの不調だけではなく、ココロの不調も原因になりやすいことをご存じでしょうか。</p>
                <p>精神的なストレスがあると、性的な刺激や興奮を陰茎に伝達する神経が正常に機能しないことがあります。喩えて言うならば、ネット回線の不具合のようなものでしょうか。つなげようという意思はあるのに、見えない障害が妨げているのです。<br>
                仕事や夫婦関係などの日常生活におけるストレスや、たまたまセックスがうまくいかなかったことをトラウマに感じるなど、その要因はさまざまです。</p>
                <p>近年、20代30代といった若い世代の方でもEDの傾向がある方が増えているのは、ストレス社会が直接原因といっても過言ではないでしょう。</p>
                <h3>混合性</h3>
                <p>器質性EDと心因性EDが混在している状態です。</p>
                <p>EDを患っている方は、器質的なことや心因的なことだけが原因ではなく、どちらの原因もあわさった症状である方が非常に多いのが現状です。<br>
                特に生活習慣病にかかわる病気や外傷があった場合に、精神的な負担も大きくなると言われています。</p>
                <h3>薬剤性</h3>
                <p>現在服用している薬の副作用としてED症状が現われている場合があります。<br>
                解熱・消炎鎮痛剤・抗不安薬など中枢神経に作用するもの、末梢神経や消化管に作用する麻酔薬・抗コリン薬・鎮けい薬などを服用されている方で、勃起維持に不調がある方は、薬剤性EDの可能性が考えられます。<br>
                循環器系に作用する降圧剤・血管拡張剤なども、それに該当する薬剤です。</p>
