                <p>早漏の定義は諸説ありましたが、現在では国際性機能学会（International　Society for Sexual Medicine, ISSM）によって成された研究結果に基づき、客観的な事実や根拠（総じて「エビデンス」という）に基づいた定義が使用されています。</p>
