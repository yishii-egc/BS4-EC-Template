                <p>早漏の治療法としては、薬物療法、セックス療法、その両方を兼ね備えた療法、もしくは早漏をコントロールするトレーニングなどの方法があります。</p>
                <p>薬物療法には内服薬と、直接陰茎に薬剤を塗布する薬剤が存在し、陰茎に直接塗布する薬剤には軟膏やスプレータイプ、ローションタイプ、コンドームの内側に薬剤が塗布されているタイプなどさまざまな形が存在します。</p>
                <p><span>早漏の改善はパートナーとの良好な関係構築、関係持続に大きな役割を担っており、男性側だけの問題ではなく、パートナーとともに乗り越えていく問題です。</span>難しい場合もあるかもしれませんが、是非治療にあたってはパートナーとともにおこないましょう。きっとパートナーとの絆を深める結果となるでしょう。</p>
                <h3>内服薬による治療法</h3>
                <p>早漏に適応がある内服薬は日本では承認されておりませんが、世界ではプリリジーという薬剤が承認されており、ヨーロッパやアジア各国で発売されています。有効成分はダボキセチンでありSSRI（選択的セロトニン再取り込み阻害薬）です。</p>
                <p>神経終末におけるセロトニンの再取り込みを阻害する作用があり、脳内のセロトニン濃度を高めることでノルアドレナリンの過剰な分泌抑制を促進し、交感神経の亢進状態を抑制することで、性的刺激への過敏状態を改善し、早漏を予防します。</p>
                <p><span>プリリジーを性行為の1時間ほど前に服用すると、30分ほどで効果が現れ、射精までの時間が3～4倍になり、効果は3～5時間ほど持続するといわれています。</span></p>
                <p>またプリリジーにはすでにジェネリック医薬品も存在していますが、日本ではいずれも未承認のため、ご利用にあたっては個人輸入してください。</p>
                <p>三環系抗うつ薬は神経終末におけるセロトニンとノルアドレナリンの再取り込みを両方阻害する作用を有し、セロトニンの脳内濃度を高めるとともに、ノルアドレナリンの過剰分泌も抑えることで早漏に効果を示すといわれています。</p>
                <h3>塗布剤による治療法</h3>
                <p>早漏の原因の1つには陰茎が刺激に対して過敏であるということがあげられます。そのため陰茎の神経を麻痺させ、刺激に鈍感にさせ、射精までの時間を長くすることで早漏の改善が期待できる治療法です。</p>
                <p>一般的には局所麻酔薬であるキシロカインやリドカイン、ベンソガインなどの有効成分を含む<span>薬剤を直接陰茎に塗布することで、陰部の刺激に対する感度が下がり、早漏を予防することが可能です。</span></p>
                <p>局所麻酔薬を有効成分に含む早漏防止薬は多岐にわたっており、スプレータイプは性行為前に陰茎に直接吹きかけて使います。</p>
                <p>性行為の何分前に吹きかけるのかは薬剤の効果発現時間により異なり、トイレなどで手軽に吹きかけることができるメリットがあります。</p>
                <p>ローションタイプはマッサージしながら陰茎に塗り込むタイプであり、塗布の際はパートナーにお願いしてもいいでしょう。またコンドームの内部に局所麻酔薬が塗布されている商品もあり、パートナーに知られたくないと考える方にもおすすめの商品です。剤型はご自身の生活様式に合い、負担のかからない方法をご選択ください。</p>
                <h3>セックス療法</h3>
                <p>性行為の経験が少ないため、陰茎が刺激に不慣れであるがゆえに早漏が起こっていると考えられる原発性の早漏に効果がある療法です。その名の通り、セックスの経験を積んで陰茎をセックスの刺激に慣れさせるという療法です。</p>
                <p><span>射精には亀頭の周囲、亀頭冠への刺激が直接関係している</span>といわれています。そのため早漏の方はまず、膣に挿入する際の刺激に耐え切れず、射精してしまう場合が少なくないので、挿入時の刺激に慣れることがまず必要です。亀頭部への刺激に慣れてきましたら、ピストン運動などの際の陰茎全体への刺激に慣れさせていきます。</p>
                <p>通常はパートナーとともに6～10回の専門医とのカウンセリングを受けながら治療を進めていきますが、治療の中では、性行為の経験を積み重ねる中で、射精しそうになったときの対処法として、いくつか方法があります。</p>
                <p>スクイーズ法は挿入直後に膣から陰茎を抜き、亀頭を数秒間圧迫し、快楽の波が過ぎ去るのを待って、性行為を再開し、射精しそうになるたび繰り返すという方法です。またスタート・ストップ法は射精しそうになったら動くのをやめ、快楽の波が過ぎ去ったらまた動き始めるという方法です。性行為の経験を積みながら、ご自身に合った方法を試してみてください。</p>
                <h3>早漏トレーニング</h3>
                <P>射精をコントロールできるようになることを目的としたトレーニングであり、さまざまなトレーニングがあります。亀頭オナニーは包皮を剥いて露出した亀頭を刺激しておこなうオナニーであり、亀頭の皮膚を鍛えることができ、膣挿入時の刺激に耐えられるようになることを目指すトレーニングです。</P>
                <p>亀頭の包皮をかぶせたままオナニーを実施すると、亀頭の皮膚を鍛えることができませんので、必ず包皮を剥くようにしてください。また女性の膣内に近い環境をカップで再現し、カップを陰茎にかぶせ弱い刺激からトレーニングを始め、徐々に強い刺激を与えていくことで、陰茎が強い刺激にも耐えることができるようにするトレーニング方法もあります。</p>
                <p>一例として、それぞれの刺激段階でマスターベーションによる刺激に10分間耐えられることを目標に、週2回の頻度で6週間の期間が設定されているものもあります。</p>
                <p><span>早漏トレーニングは、少し時間はかかるものの、実際に膣内に挿入していられる時間が延びたとの報告があがっており、</span>時間はかかりますが、費用をそれほどかけず、また薬に頼らず症状を改善させたいと願う方におすすめです。</p>
