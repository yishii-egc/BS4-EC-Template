                <h3>早漏の分類</h3>
                <p>早漏は原発性か続発性に分類することができ、原発性とはその名の通り、性経験の初期段階から早漏に悩まされている状態です。</p>
                <p>毎回もしくはほとんどの性行為において、膣挿入の前もしくは挿入直後（1分以内）に射精してしまう状態であり、射精を遅らせることができず、その結果ストレスやフラストレーションなど精神的なダメージを受けており、性行為を避ける傾向にあるといわれています。</p>
                <p>一方続発性の場合は、通常の性行為をおこなえた期間があった後に発症する早漏です。膣内挿入から射精までの時間が問題となるほど短くなってしまう状態であり、通常3分以内まで短くなるといわれています。その結果、同じくストレスやフラストレーションを抱えて、性行為を避けてしまう場合があるといわれています。</p>
                <p>原発性でも続発性でも一番症状が重い早漏は、膣挿入前に射精してしまう状態です。<br>
                その他、刺激の程度・状況・パートナーを問わず発生する早漏は一般型、ある刺激に対してのみ過敏に反応する、ある状況のみ、あるパートナーの時のみ早漏が発生する場合は状況型に分類されます。</p>
