                <h2 id="medical-01">早漏（そうろう）とは</h2>
                <p>早漏とは英語でPremature Ejaculation（プリマチュア イジャキュレーション）といい、男性の性機能障害で、女性器（膣）への<span>挿入前もしくは挿入してすぐに、意志に反して早く射精してしまう状態</span>のことを指します。射精のタイミングをコントロールすることができず、早く射精してしまうことが原因で自身、そしてパートナーにもストレスがかかってしまうこともあります。</p>
                <p>米国の18歳から59歳の男性の21％が早漏であるという報告もあり、その他の研究から世の男性の5～30％が罹患(りかん)しているといわれております。早漏の生態的な要因は不明であり、多くは身体機能に問題がない場合が多く、心因性と考えられています。</p>
              </section>

