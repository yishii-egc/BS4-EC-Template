                <p>早漏の原因は明確にはわかっていません。ですが早漏は前立腺炎などの身体の状態や、悩みなどの心理的・精神的要因、また滅多にありませんが甲状腺機能亢進症と関連していることがわかっており、勃起不全を有する男性は勃起能力が完全に失われる前に早漏を経験しているといわれています。</p>
                <p>遺伝的な要因があるのかもしれないという研究者もいる一方、脳内の化学的不均衡や受容体感受性の変化が原因の1つかもしれないと考える研究者もいます。かつては性的経験不足が射精を早めているともいわれていましたが、現在では性行為への不安や何かしらのストレスによる発症が大半であると考えられています。</p>
