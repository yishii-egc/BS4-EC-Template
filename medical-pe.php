                <h3>早漏の治療基準</h3>
                <p>ISSMは一般的に早漏とは自信の意志より早く射精してしまう男性側の性機能障害だとしており、膣内挿入から射精までの時間が短く、男性側に射精をコントロールする力がなく、男性がそのことをストレスに感じる状態としています。ちなみに<span>一般的な射精に有する時間は5～7分</span>といわれています。</p>
                <p>発症時期、膣内挿入から射精までの時間、男性自身がどれくらい射精をコントロールできると感じているか、男性は早漏の状況をどれだけストレスに感じているか、男性のパートナーは早漏をどれだけストレスに感じているか、心理的・感情的もしくは関係性について懸念事項はあるかを聴取の上、早漏と診断し、治療を開始します。</p>
                <p>基本的には性行為のほとんど（75～100％）において早漏が発現し、膣内挿入から射精まで1分以内と非常に短いか、膣内挿入前に射精してしまう状態が少なくとも6ヶ月以上継続していること、また器質的要因が見つからず、精神的ストレスが原因と考えられることが1つの基準となります。</p>
                <p>また患者がどれだけ精神的ストレスを感じているかを数値化するツールとして「早漏診断ツール」というものがあり、各質問項目を5段階で回答してもらい点数化し、診断・治療の一助として使用しています。</p>
