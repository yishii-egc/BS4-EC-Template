                <h2 id="medical-06">EDの症状</h2>
                <p>これまで何の問題もなく勃起できた人にとっては、「なぜ勃起しなくなったのか」または「勃起を持続できなくなった理由があるのか」など、勃起できなくなった理由が気になると思います。</p>
                <p>EDが起こる要因として主に<span>器質性、心因性、混合性との3つの原因に分類されます。</span><br>
                器質性は病気や怪我のような体の異常により起こり、心因性はうつ病などの精神疾患により心のバランスが崩れることが原因です。さらに器質性と心因性の2つが重なることで混合性EDが発症する恐れもあります。</p>
                <p>このように<span>勃起の維持や持続ができなくなる原因はひとつではありません。</span></p>
                <p>そこで、これらのEDを改善していくには、原因にあった治療や対策をおこなうことが大切です。<br>
                器質性であれば病気の治療をおこない、心因性であればカウンセリングをおこないます。<br>
                さらに混合性であれば両方を改善させなければいけません。</p>
                <p>EDを引き起こしている原因がわかれば、より<span>適切に治療をおこなうことが可能</span>です。<br>
                逆にいうと、原因を知っておかなければEDを悪化させてしまう可能性もあります。</p>
                <p>そこで、今後正しくEDを改善していくためにも、器質性・心因性・混合性の3つのEDの原因についてそれぞれ詳しく解説していきます。</p>

                <h3>EDの原因を4タイプに分類</h3>
                <p>勃起が維持できなくなるEDの原因はさまざまな要因があります。EDを一言でいうと、男性器の性的刺激がうまく脳に伝わらず血液の流れが悪くなった状態です。</p>
                <p>逆に性的刺激が上手に脳に伝わり、男性器へ十分に血液が流れれば、男性器は正常に勃起します。<br>
                勃起がうまくいかない、もしくは途中で柔らかくなってしまうのは、この勃起の流れを阻害している何らかの原因があるためです。</p>
                <p>しかし、この勃起するまでの流れを阻害している原因はひとつではありません。いくつかの原因が重なり合い、勃起不全の要因をつくっています。</p>
                <p>EDが発症する要因は、大きく4つに分類され、器質性による障害や心因性による障害、その他に混合性や薬剤性が原因といわれています。</p>
                <p>器質性とは病気や疾患が原因となって起こる症状であり、心因性はストレスや不安が原因となって発症します。<br>
                そして、混合性は器質性と心因性が重なっておこるために発症し、薬剤性は薬の副作用が原因となって発症します。</p>
                <p>このように勃起不全が<span>何によって発症しているのかわかれば、より早くEDを改善させることが可能</span>です。</p>
                <p>そこで、勃起の維持や持続ができない要因を知るために、それぞれの原因について詳しく解説していきます。</p>

                <h3>器質性EDとは</h3>
                <p>まず器質性から解説していきます。<br>
                <span>器質性EDとは、神経や男性器自体に問題があること</span>を指します。
                器質性EDは原因を突き止めることが治療の一番の近道です。</p>
                <p>しかし、器質性には症状の種類が多いため、検査でしっかりとEDの原因を調べる必要があります。</p>
                <p>器質性の原因は、大きく分けると血管性・神経性・内分泌性・外傷性の4つに分類されます。<br>
                まず血管性ですが、加齢や血管の老化が原因で男性器に流れる血液が流れにくくなることで発症します。<br>
                次に神経性は、神経回路や骨盤の損傷が原因で、脳と陰茎にある神経の伝達物質が流れにくくなることで発症します。</p>
                <p>この神経性EDはパーキンソン病や膀胱がん、前立腺がんなどを発症した後に起こりやすいといわれています。<br>
                そして、内分泌性EDは、男性ホルモンであるテストステロンが阻害されることで発症します。<br>
                最後の外傷性EDは、生まれつき男性器に異常があった、または手術後に勃起しなくなることが原因です。</p>
                <p>このような器質性は、<span>原因を特定させることができれば症状に合った治療をおこなうことができます。</span><br>
                内服薬や補助器具を使用し、勃起を改善していくことが可能です。<br>
                また手術が原因で一時的にEDになってしまう場合もこの器質性EDの症状に含まれます。</p> 

                <h3>心因性EDとは</h3>
                <p>次に心因性EDについて解説します。<br>
                心因性とは、<span>不安や心配事などストレスが原因となって起こる症状</span>のことをいいます。</p>
                <p>心因性の問題点は、体に何の異常もみられないため本人にも自覚がないことが多く、原因を探るのが難しいところです。<br>
                また、一人で思い悩んでしまう人も多いため、治療や相談もできないままさらに恐怖や焦燥感に悩まされ、結果として症状が重くなっていくケースもあります。</p>
                <p>心因性EDは、自覚ができるストレスがある現実心因と、自覚できないストレスを抱えている深層心因とに分類されます。<br>
                現実心因は「パートナーからの期待に応えられない」「妊娠させることにプレッシャーを感じる」「仕事で疲れている」など原因が自分でわかっている状況です。</p>
                <p>それに対し深層心因は、過去に虐待や恐怖を感じたなどのトラウマにより、自分でも気づかないうちに負った心のダメージが原因で発症します。<br>
                このような心因性によるEDの治療では、抱えている悩みが現実心因か深層心因かを突き止めることから始まります。</p>
                <p>心因性EDはEDの治療と一緒に、<span>精神分析をしながらうつ病の治療もおこなうことがあります。</span><br>
                またパートナーと協力し合い、自信をつけることで回復が早くなる傾向があります。</p>

                <h3>混合性EDとは</h3>
                <p>最後に混合性EDについて解説します。混合とは、先に紹介した<span>器質性と心因性の両方が原因となって発症するED</span>を指します。</p>
                <p>多くの場合、事故や病気などで体に何らかの異変が起こり、精神的なストレスを抱えた結果発症します。</p>
                <p>混合性EDになってしまう主な病気として、動脈硬化や糖尿病、高血圧などが原因です。<br>
                また事故や怪我が原因でEDを発症することもあります。<br>
                病気により一時的にEDになり精神的な不安や悩みを抱えてしまうことで、心因性EDを同時に発症してしまいます。</p>
                <p>また、先にうつ病などの精神的な病気がキッカケで心因性EDとなり、体の不調が深刻化し器質性EDが発症していくという逆のパターンも考えられます。</p>
                <p>混合性は心と体の2つの原因が重なるために、治療に時間がかかると思われがちですが、多くの場合は体の不調が改善できればED問題も解決できます。<br>
                また体の治療とともに精神面のケアを同時におこなうこともあります。</p>
                <p>混合性EDは<span>どのようにしてEDを発症したか原因を突き止めることが重要</span>です。</p>
                <p>また「自分は持病があるから、もしかしてEDになりやすいのかもしれない」という思い込みが精神的に負担をかけ、EDを発症してしまうケースもあるため、独りよがりにならないようにしましょう。</p>

                <h3>薬剤性EDとは</h3>
                <p>薬剤性とは、いつも飲んでいる薬が原因となって発症する状態を指します。<br>
                もし、日常的に服用している薬があれば、その薬の副作用が原因でEDを引き起こしているかもしれません。</p>
                <p>薬には主作用と副作用があり、体調や体質によって異なる方向へ薬が作用することを忘れてはいけません。</p>
                <p>副作用は、医師や薬剤師の指導通りに服用していれば命にかかわるような重篤な症状は起こりませんが、自己判断で長期に服用している薬があった場合は、知らないうちに体に何らかの症状が起こっている恐れがあります。</p>
                <p>EDに原因があると考えられている代表的な薬は、神経や血液循環に深くかかわるタイプの医薬品です。<br>
                例えば、解熱剤や抗うつ剤、抗コリン剤や血管拡張剤などのような薬剤です。</p>
                <p>このような副作用の中には、勃起不全に何らかの影響をおよぼしているものもあります。<br>
                意外と身近にある常備薬が、薬剤性EDの要因のひとつになっている可能性は少なくありません。</p>
                <p>ただし、<span>薬剤性は薬を中断すると症状が改善していきます。</span><br>
                副作用が蓄積されて男性器がダメになったというのは、非常にまれなケースです。<br>
                だからといって持病があるのに独断で薬をやめてしまうことは大変危険なので、自己判断しないよう注意しましょう。</p>
              </section>

              <section>
                <h2 id="medical-07">EDの兆し？朝立ちの理由と原因</h2>
                <p><span>勃起が起こる原因は、大きく分けると「性的な刺激による勃起」と「睡眠中に無意識に起こる勃起」</span>とに分けられます。</p>
                <p>朝起きたときや朝起きてからしばらくすると、意識していないのに自然に勃起していることがあると思います。<br>
                若いころは頻繁にあった朝立ちも、加齢とともに減少してしまい、朝立ちがないことがEDの症状かもしれないと不安になったことがある人もいらっしゃるでしょう。</p>
                <p>朝立ちは起きたときに1回のみ起こる現象ではなく、寝ている間も繰り返されている自然な現象です。<br>
                自分には朝立ちがないと思っている人は、その寝起きの1回がないだけで、実は夜中に何度も繰り返されているのです。</p>
                <p>これまで、性的な刺激による勃起について紹介してきましたが、ここからは睡眠中に無意識に起こる勃起、つまり朝立ちについて解説していきます。</p>
                <p>朝立ちとEDはとても深い関係をもっており、泌尿器科やメンズクリニックではEDを調べる検査のひとつとして、朝立ちが正常におこなわれているかどうかを測定することもあります。<br>
                ここからはEDを深く知るために、朝立ちについて詳しくみていきましょう。</p>
                <p>この夜間陰茎勃起現象がなぜ起こるのか、そしてEDとどう関係があるのかさらに解説していきます。</p>

                <h3>年代別の傾向</h3>
                <p>男性にとって朝立ちは生理的な現象ですが、年齢を重ねるごとに回数が減少したり勃起に気がつくことが少なくなったりします。<br>
                一般的に朝立ちに年齢制限はなくいつまでも続きますが、加齢とともに朝立ちに気づく回数や硬度が減少します。</p>
                <p>朝立ちは血流やホルモンの影響を受けやすいため、このように加齢とともに変化していくのは仕方のないことですが、逆にいうと朝立ちが悪い、もしくは朝立ちがないという人は<span>血管の状態が悪かったり男性ホルモンが減少したりする傾向があります。</span></p>
                <p>そこで朝立ちが正常におこなわれているかを確認するために、年齢別に朝立ちの傾向を紹介していきますので参考にしてみてください。</p>
                <p>まず、20～30代は寝ている時間の半分が朝立ち、つまり夜間陰茎勃起が起こっています。<br>
                通常の状態よりも3センチほど大きくなることが一般的です。</p>
                <p>40～50代になると、朝立ちの回数は夜間の1/3～1/4に減少します。<br>
                また通常の状態よりも2センチほど大きくなっています。</p>
                <p>60代以降は朝立ちに気づきにくくなるものの、80～90代になっても健康な人であれば夜間陰茎勃起は正常に起こっています。</p>
                <p>このように夜間陰茎勃起は、年齢問わず起こるものですが、もしも40代以降の男性が朝立ちに気づかなかった場合、更年期障害が疑われます。<br>
                また60代以降であれば熟年期障害の可能性も否定できません。</p>


                <h3>朝立ちの理由や原因</h3>
                <p>これは性的な刺激や感覚がない全くの無意識の状態で、男性器が勃起してしまう現象です。<br>
                実は、夜間陰茎勃起現象は夜寝ている間に何度も繰り返されており、<span>男性だけでなく女性にも起こっている生理現象</span>なのです。</p>
                <p>この夜間陰茎勃起現象は一体どれくらいの頻度で発生しているのかを調べてみました。<br>
                京都大学の研究によると、正常な成人では1時間20～25分の感覚で約20～40分程度、勃起現象が続いています。<br>
                この現象は若い世代ほど多くみられ、高齢者だと勃起現象は比較的少ない結果となっています。</p>
                <p>夜間陰茎勃起現象は、眠りが浅いレム睡眠時に起こります。</p>
                <p>レム睡眠は、体は寝ているけど頭は起きているという状態で、一般的に夢をみていることが多い状態といわれています。<br>
                人はレム睡眠時から覚醒することが多いため、レム睡眠時に起こっていた朝立ちに気がつきやすくなっています。</p>
                <p>つまり、朝立ちを確認できなくても夜間陰茎勃起現象は発生しているため、<span>朝立ちがないから自分の体がおかしいということにはなりません。</span><br>
                しかし、逆に朝立ちしているから正常に夜間陰茎勃起現象が起こっていることにもなりません。</p>


                <h3>朝立ちしなくなったらED？</h3>
