                過度なストレスやトラウマ、うつ病が原因で発症する<span>心因性のEDにはカウンセリングが有効</span>とされています。</p>
                <p>カウンセリングは、心に抱えている問題がどのようにして現れたのかカンウセラーと一緒に探し出していきます。</p>
                <p>「仕事や疲労により性行為をする時間がとれな」「パートナーからの過度の期待に応えられない」「過去にトラウマがある」など、どのような問題がED患者の心を苦しめているのか原因を探り出し問題解決まで導いてくれます。</p>
                <p>カウンセリングは、自覚がない問題点に気がつくことができる一方で、原因を探り出し改善効果が現れるまでに何度か通院し面談に時間がかかるという点もあります。<br>
                一般的にカウンセリングだけで改善が見られない場合は、薬物療法と併用して治療をおこなっていきます。</p>
                <p>カウンセリングで一番大切なことは、カウンセラーとの相性です。<br>
                相性のいいカウンセラーを見つけるポイントは、アドバイスよりも話を聞いてくれる、患者を理解しようとする姿勢があるという点です。</p>
                <p>安心して心の悩みを打ち明けることができるカウンセラーを探すために、まずはクリニックの情報収集から始めましょう。</p>
              </section>

              <section>
                <h2 id="medical-16">EDの克服</h2>
                <p>EDを克服するためには、<span>まずは自分がEDであると認めることが第一歩</span>になります。<br>
                もしもパートナーがいる場合には、ひとりで思い悩まずに思い切って悩みを打ち明けてみるもの克服へつながるひとつの道です。</p>
                <p>これまでご説明してきたように、男性は思春期から亡くなるまで勃起することが可能です。<br>
                ただ「勃起しない」というだけでなく、満足のいくような性行為ができないことや硬度が保てないという悩みもEDの深刻な症状のひとつです。</p>
                <p>このような病状は加齢や体質が原因というわけではなく、血管や神経伝達物質が何らかの形でうまく流れていないことが原因です。</p>
                <p>このように男性にとってEDは体の不調のひとつであり、勃起不全という疾患に罹っていると考えましょう。<br>
                体の不調であれば、正しい治療や薬を使い、改善させていかなければいけません。<br>
                EDを認めて治療していくことは、恥ずかしがることはありません。</p>
                <p>EDの症状はバラバラで、勃起がうまくいかない日もあれば逆に満足な性行為ができる日もあります。<br>
                そのため「この程度であれば相談する必要もないだろう」とEDを軽視してしまうひともいます。</p>
                <p>しかしEDは自分でも気がつかないうちに進行していく恐れがあるため、少しでもEDの自覚があれば放置せずにまずはパートナーに打ち明けてみましょう。</p>


                <h3>ひとりで思い悩まない</h3>
                <p><span>ストレスはEDを悪化させてしまう要因のひとつ</span>です。<br>
                男性にとってEDを打ち明けることはなかなか難しいことではありますが、ひとりで思い悩んでいても改善するばかりか、EDの症状が進行していく恐れがあります。</p>
                <p>国内ではED患者は男性の3人に1人といわれています。<br>
                日本臨床内科医師会の統計によると、ED患者の数は増え続け、2016年の時点で1,130万人、現在では2000万人近くの男性がEDの症状に悩んでいるというデータがでました。</p>
                <p>実際にEDの症状に気が付いてはいても、「はずかしい」「かっこわるい」「年寄りみたいだ」という思いが先行し打ち明けることが遅くなった結果、EDが進行しているというケースが多いようです。</p>
                <p>確かに、加齢や体質はEDのリスクファクターのひとつではありますが、他にも病気や薬害など、あなた自身の問題だけではない原因が複雑に絡み合っていることも考えられます。<br>
                逆に考えると、EDは血管や神経の異常を知らせてくれる病気のサインかもしれません。</p>
                <p>「性行為の途中で中折れしてしまう」「勃起が維持できない」「思うように硬くならない」という症状がみられた場合には、まずは医師かパートナーかに打ち明けてみましょう。</p>

                <h3>パートナーに理解してもらうためには</h3>
                <p>早期にEDを治療するためには、パートナーの理解を得ることが大切です。<br>
                EDは体の不調によって起こる病気のひとつであり、治療すれば改善するものだと、パートナーに打ち明けてみましょう。</p>
                <p>性行為中に勃起不全の症状が起こると、パートナーにも精神的な負担を感じさせてしまう恐れがあります。</p>
                <p>EDを発症してしまうと性行為から疎遠になってしまうケースもありますが、パートナーがいる場合、このような状態をずっと続けていくことで今後の生活に支障をきたすかもしれません。</p>
                <p>EDを打ち明けることは、とても勇気がいることですが、<span>EDは自己判断で改善することが難しい病気</span>です。<br>
                問題を遠ざけ放置しても解決することはありません。むしろ、EDを打ち明けないことで余計なトラブルを招いてしまう可能性も考えられます。</p>
                <p>このような事態を避けるためにも、「こういう症状に悩んでいる」と思い切って打ち明けてみましょう。</p>
                <p>パートナーに余計な心配をかけずに済む方法としては、「EDがなぜ起こるのか」「EDは治る病気」「パートナーの責任ではない」ということを相手にしっかりと伝え、今後どのようにしてEDに向き合っていくのか話し合いましょう。</p>
                <p>相手に伝えにくい場合は、一緒に通院して話を聞いてもらうのもひとつの方法です。</p>
              </section>

              <section>
                <h2 id="medical-17">EDによくある悩み</h2>
                <p>現代社会においてED患者は増え続けていますが、実は江戸時代にもEDは重大な病気として存在しました。<br>
                EDは腎虚と呼ばれ、強度の心身衰弱を意味し「男性機能の終わり」と不安をもたらす病の一種でした。<br>
                腎虚は免疫機能や内臓機能が低下することで、生殖能力や精力減退などの症状があらわれる状態です。</p>
                <p>腎虚にかかると、「男性器から最後に赤い玉がでる」「早死にする」という根拠のない噂話が流行したといわれています。</p>
                <p>そんな腎虚が恐れられていた江戸時代、かの有名な徳川家康も健康を気遣い精力増強剤を活用していたといわれています。</p>
                <p>徳川家康は60代まで子作りに励み、結果18人以上の子を持ったといわれています。<br>
                徳川家康は、「一粒金丹」というオットセイの睾丸を精力増強剤として取り寄せていたそうです。</p>
                <p>また、海外の歴史ではEDにかかると社会不適格者といわれるため、初夜の性行為を恐れる男性もたくさんいました。<br>
                股間を強調する男性服や、男性器をあらわす像が多かったのも男らしさを追い求めたためといわれています。<br>
                実際のところ、ルイ16世やヒトラー、ニュートンもEDに悩んでいたと伝えられており、はるか昔からEDは男性にとって「不能」という烙印を押されてしまう恐怖の症状だったようです。</p>
              </section>

              <section>
                <h2 id="medical-18">ED（勃起不全）まとめ</h2>
                <p>EDの症状は、ただ勃起しないというだけでなく勃起の持続時間や硬度の低下もみられます。<br>
                「自分は勃起障害かもしれない」という不安を感じたら、自宅でも簡単にセルフチェックすることができるので試してみましょう。</p>
                <p>勃起できなくなる原因は、病気や生活習慣、ストレスなどいくつかの要因により発症してしまいます。<br>
                中には自覚症状がない場合もあるため、自分でも気づかない内に症状が悪化していくこともあります。</p>
                <p>男性にとって<span>勃起は健康状態を確認するバロメーターのようなもの</span>です。<br>
                血液の流れに左右される勃起不全には、思わぬ大病が潜んでいるかもしれません。</p>
                <p>もし男性器に不調を感じたり満足のいく性行為ができなかったりする場合は、「こんな程度なら自然に改善するだろう」と思わずに、EDとしっかりと向き合ってみましょう。</p>

