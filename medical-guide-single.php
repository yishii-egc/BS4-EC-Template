                <h2 id="medical-05">EDの原因</h2>
                <p>EDは身近な人にこそ相談しにくく、「自分はED気味ではないか」と不安を抱えていても、治療をすることに抵抗を感じてしまう人も少なくありません。<br>
                また<span>EDは男性の体の不調を訴えるサイン</span>ともいわれています。</p>
                <p>「勃起しなければED、それ以外は大丈夫」と思われがちな勃起不全ですが、その症状は幅広く、例えば性行為の途中で男性器が柔らかくなってしまうこともEDの初期症状です。<br>
                深刻に悩むほどの症状でもないと勝手に判断してしまうことで、EDが進行してしまうケースもあります。</p>
                <p>このように、<span>EDの症状を知ることで早めに対策が取れたり、悪化することを防いだりすることも可能</span>です。</p>
                <p>もし、性行為を満足に終わらせる自信がないときは、ご自身がEDかどうかセルフチェックしてみましょう。<br>
                相談することが気恥ずかしく、メンズクリニックや泌尿器科に行く勇気がないときは、ご自宅で気軽にED症状の確認をしてみてください。</p>
                <p>ご自身が不調かどうかを知ることができれば、専門機関に相談しにいくこと、また<a href="/item-list.php?category=ED" target="_blank">ED治療薬</a>を購入することなど改善するための第一歩を踏み出せるかもしれません。</p>
                <p>そこで、どういう症状が勃起障害といわれるのか、または自分がEDかどうかを確認する方法を紹介します。</p>

                <h3>診断の定義</h3>
                <p>まずは、勃起不全の診断の定義について紹介します。勃起不全と勃起障害は同じ意味となり、勃起時の男性器の硬度が不足している、勃起を維持できないなどの状態をいいます。<br>
                しかし、ただ「性行為ができない」「パートナーを満足させられない」という事実から勃起不全と判断するのは間違いです。</p>
                <p>このような勃起不全と診断されるには、勃起障害の症状を自覚していること・病歴・陰茎の状態・検査結果を総合して判断することが必要です。</p>
                <p>勃起障害の診察ができる医療機関は、主に泌尿器科やメンズクリニックです。<br>
                医療機関では、まず勃起不全につながる病歴はなかったかを問診します。</p>
                <p>病歴については糖尿や脊髄系の病気だけでなく、うつや不安、性行為に対する悩みなど、心理的要因があるかも聴取されます。</p>
                <p>また生活習慣もEDに深くかかわってきますので、普段の運動量や食事内容、喫煙や飲酒の量も聞かれます。<br>
                そして、男性器の状態を見るための触診などがあります。</p>
                <p>このとき、ホルモンのバランスが崩れている要因があれば、勃起を起こす男性ホルモンの状態を測定することもあります。</p>
                <p>このように体や心の状態を総合的に判断した結果、勃起不全または勃起障害という診断が下されます。</p>

                <h3>症状</h3>
                <p>EDいわゆる勃起不全とは、ただ男性器の勃起が起こらないという症状だけではありません。<br>
                勃起はするものの十分な硬さが得られないことや、すぐに中折れしてしまうこともEDの深刻な症状のひとつです。</p>
                <p>満足する勃起状態を保てないことで、性行為に支障が出てしまう状態をまとめて勃起不全といいます。</p>
                <p>EDの症状にも段階があり、初めから全く勃起しなくなったというのは非常にまれなケースです。<br>
                「性行為の始めは良かったけれど、途中で元気がなくなった」という初期症状に始まり、次に「十分に硬くならないまま射精してしまう」といった症状も出てくるようになります。</p>
                <p>さらにEDが進行していくと、射精が出来なかったり、勃起が出来なかったりと性行為を満足におこなうことが難しくなります。</p>
                <p><span>EDの怖いところは、精神的な負担も症状に影響するところ</span>です。<br>
                満足な勃起ができないことで男性としての自信を失くし、さらにEDの症状が深刻化してしまうという悪循環を引き起こしてしまいます。</p>
                <p>このように性欲は十分にあるのに勃起に時間がかかったり、勃起状態にムラがあったり、性行為の途中で柔らかくなってしまうことも、EDの症状に含まれます。<br>
                このように勃起に少しでも不安を感じてしまう場合は、EDの初期症状かもしれません。</p>

                <h3>セルフチェック</h3>
                <p>それでは実際にEDかどうかセルフチェックする方法を紹介していきます。<br>
                勃起不全の傾向があるかどうかは、IIEFという国際勃起機能スコアというセルフチェックシートを使用し、投薬や治療の判断がされます。<br>
                過去6カ月以内の男性器の様子を思い出しながら自分がどのような状態にあるのか調べていきましょう。</p>
                <p>セルフチェックのポントは5点で、「勃起を維持できるかどうか」「挿入できる硬さになるまでの時間」「挿入後に勃起を維持できるか」「挿入中に勃起を維持するのを困難に感じたか」「性行為に満足できかたどうか」をそれぞれチェックしていきます。</p>
                <p>まずは「勃起を維持できるかどうか」ですが男性器の勃起を維持できる自信があったかどうか5段階にわけてチェックします。<br>
                自信があったら5点・やや自信があれば4点・普通であれば3点・やや自信がなければ2点・自信がないのであれば1点と点数をつけましょう。</p>
                <p>これを「硬さ」「勃起の維持」など、他の4項目でも同じように点数をつけていきます。</p>
                <p>合計点数が21点以下であればEDの傾向がみられ、この結果は、病院でも診断の参考に使用されていますので、勃起状態に関して気になることがあれば、ぜひ一度試してみてください。</p>
              </section>

