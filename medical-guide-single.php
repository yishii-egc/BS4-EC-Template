                <h2 id="medical-04">ED（勃起不全）の病因</h2>
                <p>自分がEDかどうか知るために、勃起不全がどのような障害かをわかりやすく解説していきます。</p>
                <p>勃起不全は勃起をすることができない症状を指しますが、まずは<span>これまで勃起をしたことがあるのか</span>、または<span>何度か勃起した経験はあるが満足のいく性行為に至っていないのかを確認</span>してみましょう。</p>
                <p>勃起不全は、原発性EDと続発性EDの2つの症状に分類されます。<br>
                いままで一度も勃起を経験したことのない場合は原発性EDと診断され、一度は男性器を勃起できたことのある場合は持続性EDです。<br>
                この2つのEDのうち、原発性EDよりも持続性ED患者が多い傾向にあります。</p>
                <p>EDだった場合は、<a href="/item-list.php?category=ED" target="_blank">ED治療薬</a>のような内服薬を服用しカウンセリングをおこなうことで、満足のいく性行為をおこなえるようになります。早めにEDと自覚し治療を受けることで、男性としての自信を回復させることができます。</p>
                <p>しかし、満足のいく性行為ができないとはいっても、勃起できない回数が極めて少ない場合は勃起不全ではありません。<br>
                また年齢とともに性欲が少なくなっていくのは自然のことですので、治療が必要な勃起不全と診断されない可能性があります。</p>
                <p>そこで、勃起不全と診断される定義症状、セルフチェック方法について解説していきます。</p>

