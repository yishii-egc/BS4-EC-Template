              <section>
                <h2 id="medical-03">勃起のメカニズム</h2>
                <figure class="section_eyecatch float-right ml-2 mb-2">
                  <img src="images/medical-guide/single/medical-eyecatch03.jpg" alt="" class="img-fluid img-thumbnail">
                </figure>
                <p>男性としての自信を失わせる要因になっているEDですが、EDと正常な勃起とでは何が違うのでしょうか。<br>
                硬さが足りなかったり中折れしたりするように男性器が正常に勃起しない原因は、男性として致命的な何かが不足しているから起こるのではありません。</p>
                <p>実は、<span>勃起時に発生する体内物質のバランスが崩れることでEDが起こってしまいます。</span></p>
                <p>男性器が正常に勃起している場合、性的刺激をキッカケとして脳が陰茎へと信号を送り、血液が男性器に集まります。<br>
                この集まった血液が挿入可能な程度まで男性器を勃起させる仕組みです。</p>
                <p>ところで、正常な勃起には硬度も重要なポイントとなりますが、アメリカで開発されたEHSによると、性行為をするために必要な硬度は、果物にたとえるとリンゴのような硬さだといわれています。</p>
                <p>EDでは、この勃起の流れがスムーズにいかないこと、またリンゴのような硬度を保つことが難しくなります。<br>
                硬度を保てないと、男性器は膣圧に負け、性行為の途中で抜けてしまい、挿入し続けることが難しくなってしまいます。</p>
                <p>このようにEDを知るためには、勃起の仕組みを知ることが大事です。<br>
                そこで、勃起のメカニズムと硬さ指標についてこれから詳しく解説していきます</p>
                <h3>メカニズム</h3>
                <p><span>EDを改善させるためには、「男性器の勃起」と「勃起の維持」の2つのメカニズムを知ることが大切</span>なポイントです。</p>
                <p>勃起のメカニズムを理解するために、男性器の内部はどうなっているかをおさらいしましょう。<br>
                男性器は、1本の尿道海綿体と左右にある2つの陰茎海綿体からできています。<br>
                勃起にはこの2つの陰茎海綿体の働きが重要です。</p>
                <p>男性が視覚・聴覚・味覚・匂い・想像などにより性的興奮を起こすと、勃起中枢が働き男性器の海綿体という血管へ血液が大量に送られ、陰部が硬くなり勃起が起こります。<br>
                この勃起は、射精や性的刺激の中断によって血液が海綿体から流れ出すまで続きますが、EDの症状では射精が起こる前に硬度が保てずに中折れしてしまうことがあります。</p>
                <p>この中折れしてしまう原因となるのは、勃起を維持させている物質と勃起を納める物質のアンバランスが原因です。<br>
                勃起を維持させているcGMP（環状グアノシン一リン酸）という血管拡張物質が、PDE5（ホスホジエステラーゼ5）という物質によって壊されることで、勃起が収まります。</p>
                <p>本来であれば、PDE5は性的興奮が収まったことをキッカケにcGMPを壊していきますが、EDの場合はcGMPがさほど増えていないのに、PDE5が放出されてしまう状態になります。</p>
                <p>つまり、EDの大きな原因には勃起を抑えるPDE5が深くかかわっています。</p>

                <h3>硬さ指標EHS</h3>
                <p>ところで、EDには、満足な性交渉をおこなうほどの硬度を維持できないという症状もありますが、今度は勃起を維持させるために、硬さ指標EHSを知っておきましょう。</p>
                <p>EHSはErection Hardness Score（エレクションハードネススコア）の略で、勃起時の硬さがどの程度あるかをセルフチェックするテストです。<br>
                このEHSはアメリカで開発された評価スケールで、信頼性が高く日本の医療現場でも使用されています。</p>
                <p>硬さ指標は5段階に分けられており、勃起状態の男性器の硬さはどの程度なのかを調べます。<br>
                EDかどうかを確認するためにも、さっそくセルフチェックしてみましょう。<br>
                グレードをよりイメージしやすくするために、食べ物に例えることもできます。</p>
                <p>陰茎が大きくならない状態をグレード0、陰茎は大きくなるが硬くならない状態をグレード1（こんにゃくのような硬さ）、陰茎は硬いが挿入が難しい状態がグレード2（みかんのような硬さ）、陰茎は硬いが完全ではない状態がグレード3（グレープフルーツのような硬さ）、最後に陰茎が完全に硬直している状態をグレード4（りんごのような硬さ）に分類していきます。</p>
                <p><span>グレード3以下で尚且つ挿入が難しいようであれば、ED治療対象の可能性</span>があります。</p>
              </section>

